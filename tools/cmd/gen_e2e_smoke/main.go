// Command gen_e2e_smoke scans generated packages for proxy constructors and generates
// tests/e2e/smoke_mock_all_test.go — a single E2E test file that exercises
// every generated proxy type via the mock binder.
package main

import (
	"bufio"
	"bytes"
	"fmt"
	"go/format"
	"os"
	"os/exec"
	"path/filepath"
	"regexp"
	"sort"
	"strings"
)

// generatedDirs lists the top-level directories containing generated code.
var generatedDirs = []string{
	"android",
	"com",
	"fuzztest",
	"libgui_test_server",
	"parcelables",
	"src",
}

type proxyInfo struct {
	// pkgDir is the directory relative to the module root, e.g. "android/os".
	pkgDir string
	// importPath is the full Go import path.
	importPath string
	// alias is a unique import alias for this package.
	alias string
	// proxyName is the constructor name without "New" prefix, e.g. "PowerManagerProxy".
	proxyName string
}

var constructorRe = regexp.MustCompile(`^func New(\w+Proxy)\(`)

func main() {
	root := "."
	if len(os.Args) > 1 {
		root = os.Args[1]
	}

	// Collect scan directories that exist.
	var scanDirs []string
	for _, d := range generatedDirs {
		dir := filepath.Join(root, d)
		if info, err := os.Stat(dir); err == nil && info.IsDir() {
			scanDirs = append(scanDirs, dir)
		}
	}

	if len(scanDirs) == 0 {
		fmt.Fprintln(os.Stderr, "no generated directories found")
		os.Exit(1)
	}

	// Find all proxy constructors.
	args := append([]string{"-rn", "func New.*Proxy(", "--include=*.go"}, scanDirs...)
	cmd := exec.Command("grep", args...)
	out, err := cmd.Output()
	if err != nil {
		if exitErr, ok := err.(*exec.ExitError); ok && exitErr.ExitCode() == 1 {
			fmt.Fprintln(os.Stderr, "no proxy constructors found")
			os.Exit(1)
		}
		fmt.Fprintf(os.Stderr, "grep failed: %v\n", err)
		os.Exit(1)
	}

	// Parse results.
	type pkgProxy struct {
		dir       string
		proxyName string
	}

	var entries []pkgProxy
	scanner := bufio.NewScanner(bytes.NewReader(out))
	for scanner.Scan() {
		line := scanner.Text()
		// Skip test files.
		if strings.Contains(line, "_test.go:") {
			continue
		}

		// Format: android/os/file.go:NNN:func NewFooProxy(
		colonIdx := strings.Index(line, ":")
		if colonIdx < 0 {
			continue
		}
		filePath := line[:colonIdx]

		// Extract the function signature part after the last colon pair.
		rest := line[colonIdx+1:]
		colonIdx2 := strings.Index(rest, ":")
		if colonIdx2 < 0 {
			continue
		}
		funcPart := rest[colonIdx2+1:]

		matches := constructorRe.FindStringSubmatch(funcPart)
		if matches == nil {
			continue
		}
		proxyName := matches[1]

		dir := filepath.Dir(filePath)
		entries = append(entries, pkgProxy{dir: dir, proxyName: proxyName})
	}

	// Sort for deterministic output.
	sort.Slice(entries, func(i, j int) bool {
		if entries[i].dir != entries[j].dir {
			return entries[i].dir < entries[j].dir
		}
		return entries[i].proxyName < entries[j].proxyName
	})

	// Build import aliases. We need unique aliases for each package dir.
	aliasCounter := map[string]int{}
	dirToAlias := map[string]string{}

	for _, e := range entries {
		if _, ok := dirToAlias[e.dir]; ok {
			continue
		}
		alias := buildAlias(e.dir)
		if count, exists := aliasCounter[alias]; exists {
			aliasCounter[alias] = count + 1
			alias = fmt.Sprintf("%s%d", alias, count+1)
		} else {
			aliasCounter[alias] = 1
		}
		dirToAlias[e.dir] = alias
	}

	// Build proxyInfo list.
	const modulePath = "github.com/xaionaro-go/binder"
	var proxies []proxyInfo
	for _, e := range entries {
		importPath := modulePath + "/" + filepath.ToSlash(e.dir)
		proxies = append(proxies, proxyInfo{
			pkgDir:     e.dir,
			importPath: importPath,
			alias:      dirToAlias[e.dir],
			proxyName:  e.proxyName,
		})
	}

	// Generate the test file.
	var buf bytes.Buffer
	buf.WriteString("// Code generated by tools/cmd/gen_e2e_smoke. DO NOT EDIT.\n\n")
	buf.WriteString("//go:build e2e\n\n")
	buf.WriteString("package e2e\n\n")

	// Collect unique imports.
	type importEntry struct {
		alias string
		path  string
	}
	seen := map[string]bool{}
	var imports []importEntry
	for _, p := range proxies {
		if seen[p.importPath] {
			continue
		}
		seen[p.importPath] = true
		imports = append(imports, importEntry{alias: p.alias, path: p.importPath})
	}

	// Sort imports by path.
	sort.Slice(imports, func(i, j int) bool {
		return imports[i].path < imports[j].path
	})

	buf.WriteString("import (\n")
	buf.WriteString("\t\"testing\"\n\n")
	buf.WriteString("\t\"github.com/xaionaro-go/binder/binder\"\n")
	buf.WriteString("\t\"github.com/xaionaro-go/binder/tools/pkg/testutil\"\n\n")
	for _, imp := range imports {
		fmt.Fprintf(&buf, "\t%s %q\n", imp.alias, imp.path)
	}
	buf.WriteString(")\n\n")

	// Generate the proxyEntry type and slice.
	buf.WriteString("type mockProxyEntry struct {\n")
	buf.WriteString("\tname        string\n")
	buf.WriteString("\tconstructor func(binder.IBinder) interface{}\n")
	buf.WriteString("}\n\n")

	buf.WriteString("var allMockProxies = []mockProxyEntry{\n")
	for _, p := range proxies {
		fmt.Fprintf(&buf, "\t{%q, func(b binder.IBinder) interface{} { return %s.New%s(b) }},\n",
			p.alias+"."+p.proxyName, p.alias, p.proxyName)
	}
	buf.WriteString("}\n\n")

	// Generate the test function.
	buf.WriteString("func TestE2E_MockSmokeAllProxies(t *testing.T) {\n")
	buf.WriteString("\tmock := testutil.NewMockBinder()\n")
	buf.WriteString("\tvar totalMethods, passedMethods, panickedMethods int\n\n")
	buf.WriteString("\tfor _, entry := range allMockProxies {\n")
	buf.WriteString("\t\tt.Run(entry.name, func(t *testing.T) {\n")
	buf.WriteString("\t\t\tproxy := entry.constructor(mock)\n")
	buf.WriteString("\t\t\tresult := testutil.SmokeTestAllMethods(t, proxy)\n")
	buf.WriteString("\t\t\ttotalMethods += result.Total\n")
	buf.WriteString("\t\t\tpassedMethods += result.Passed\n")
	buf.WriteString("\t\t\tpanickedMethods += result.Panicked\n")
	buf.WriteString("\t\t})\n")
	buf.WriteString("\t}\n\n")
	buf.WriteString("\tt.Logf(\"TOTAL: %d proxy types, %d methods exercised, %d passed, %d panicked\",\n")
	buf.WriteString("\t\tlen(allMockProxies), totalMethods, passedMethods, panickedMethods)\n")
	buf.WriteString("}\n")

	// Format with gofmt.
	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		fmt.Fprintf(os.Stderr, "gofmt failed: %v\nwriting unformatted output\n", err)
		formatted = buf.Bytes()
	}

	outPath := filepath.Join(root, "tests", "e2e", "smoke_mock_all_test.go")
	if err := os.WriteFile(outPath, formatted, 0644); err != nil {
		fmt.Fprintf(os.Stderr, "failed to write %s: %v\n", outPath, err)
		os.Exit(1)
	}

	fmt.Printf("Generated %s with %d proxy types from %d packages\n",
		outPath, len(proxies), len(imports))
}

// buildAlias creates a unique import alias from a directory path like
// "android/os" -> "Os" or "android/hardware/bluetooth/audio" -> "HwBtAudio".
func buildAlias(dir string) string {
	rel := filepath.ToSlash(dir)

	parts := strings.Split(rel, "/")
	var alias strings.Builder
	alias.WriteString("gen")

	for _, p := range parts {
		// Apply abbreviations for common prefixes.
		p = abbreviate(p)
		if p == "" {
			continue
		}
		// Capitalize first letter.
		alias.WriteString(strings.ToUpper(p[:1]))
		alias.WriteString(p[1:])
	}

	result := alias.String()
	if result == "gen" {
		return "genRoot"
	}
	return result
}

func abbreviate(s string) string {
	switch s {
	case "android":
		return ""
	case "hardware":
		return "hw"
	case "bluetooth":
		return "bt"
	case "automotive":
		return "auto"
	case "biometrics":
		return "bio"
	case "security":
		return "sec"
	case "internal_":
		return "internal"
	default:
		return s
	}
}
