package main

import (
	"flag"
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"sort"
	"strings"

	"gopkg.in/yaml.v3"

	"github.com/xaionaro-go/binder/tools/pkg/servicemap"
)

// constantSpec describes one set of Java constants to extract and generate Go code for.
type constantSpec struct {
	JavaFile      string `yaml:"java_file"`
	Pattern       string `yaml:"pattern"`
	GoPackage     string `yaml:"go_package"`
	GoType        string `yaml:"go_type"`
	GoOutput      string `yaml:"go_output"`
	NameTransform string `yaml:"name_transform"`
}

func main() {
	frameworksBase := flag.String("frameworks-base", "tools/pkg/3rdparty/frameworks-base", "Path to the AOSP frameworks-base directory")
	configPath := flag.String("config", "tools/cmd/genconstants/constants.yaml", "Path to the YAML config file")
	outputBase := flag.String("output-base", ".", "Base directory for output files")
	flag.Parse()

	if err := run(*frameworksBase, *configPath, *outputBase); err != nil {
		fmt.Fprintf(os.Stderr, "Error: %v\n", err)
		os.Exit(1)
	}
}

func run(
	frameworksBase string,
	configPath string,
	outputBase string,
) error {
	configData, err := os.ReadFile(configPath)
	if err != nil {
		return fmt.Errorf("reading config %s: %w", configPath, err)
	}

	var specs []constantSpec
	if err := yaml.Unmarshal(configData, &specs); err != nil {
		return fmt.Errorf("parsing config %s: %w", configPath, err)
	}

	for _, spec := range specs {
		if err := generateConstants(frameworksBase, outputBase, spec); err != nil {
			return fmt.Errorf("generating constants for %s: %w", spec.GoType, err)
		}
	}

	return nil
}

func generateConstants(
	frameworksBase string,
	outputBase string,
	spec constantSpec,
) error {
	javaPath := filepath.Join(frameworksBase, spec.JavaFile)
	src, err := os.ReadFile(javaPath)
	if err != nil {
		return fmt.Errorf("reading %s: %w", javaPath, err)
	}

	allConstants := servicemap.ExtractStringConstants(string(src))

	matched := filterByPattern(allConstants, spec.Pattern)
	if len(matched) == 0 {
		return fmt.Errorf("no constants matching pattern %q found in %s", spec.Pattern, spec.JavaFile)
	}

	transform, err := parseNameTransform(spec.NameTransform)
	if err != nil {
		return fmt.Errorf("parsing name_transform %q: %w", spec.NameTransform, err)
	}

	type constEntry struct {
		GoName string
		Value  string
	}

	entries := make([]constEntry, 0, len(matched))
	for javaName, value := range matched {
		goName := transform(javaName)
		entries = append(entries, constEntry{
			GoName: goName,
			Value:  value,
		})
	}

	sort.Slice(entries, func(i, j int) bool {
		return entries[i].GoName < entries[j].GoName
	})

	var buf strings.Builder
	buf.WriteString("// Code generated by genconstants. DO NOT EDIT.\n\n")
	fmt.Fprintf(&buf, "package %s\n\n", spec.GoPackage)
	fmt.Fprintf(&buf, "type %s string\n\n", spec.GoType)
	buf.WriteString("const (\n")
	for _, e := range entries {
		fmt.Fprintf(&buf, "\t%s %s = %q\n", e.GoName, spec.GoType, e.Value)
	}
	buf.WriteString(")\n")

	formatted, err := format.Source([]byte(buf.String()))
	if err != nil {
		return fmt.Errorf("formatting generated Go source: %w", err)
	}

	outPath := filepath.Join(outputBase, spec.GoOutput)
	if err := os.MkdirAll(filepath.Dir(outPath), 0o755); err != nil {
		return fmt.Errorf("creating output directory: %w", err)
	}

	return os.WriteFile(outPath, formatted, 0o644)
}

// filterByPattern returns constants whose names match the given glob pattern.
func filterByPattern(
	constants map[string]string,
	pattern string,
) map[string]string {
	result := make(map[string]string)
	for name, value := range constants {
		matched, err := filepath.Match(pattern, name)
		if err != nil {
			continue
		}
		if matched {
			result[name] = value
		}
	}
	return result
}

// nameTransformFunc converts a Java SCREAMING_SNAKE_CASE constant name to a Go PascalCase name.
type nameTransformFunc func(javaName string) string

// parseNameTransform parses a name_transform directive and returns the corresponding transform function.
func parseNameTransform(directive string) (nameTransformFunc, error) {
	switch {
	case strings.HasPrefix(directive, "strip_suffix:"):
		suffix := strings.TrimPrefix(directive, "strip_suffix:")
		return makeStripSuffixTransform(suffix), nil
	case directive == "":
		return screamingSnakeToPascal, nil
	default:
		return nil, fmt.Errorf("unknown name_transform directive: %s", directive)
	}
}

// makeStripSuffixTransform returns a transform that strips the given suffix (with underscore prefix)
// from SCREAMING_SNAKE names, converts the remainder to PascalCase, and appends the suffix in PascalCase.
//
// For example, with suffix "PROVIDER":
//
//	NETWORK_PROVIDER -> Network + Provider -> NetworkProvider
//	GPS_HARDWARE_PROVIDER -> GpsHardware + Provider -> GpsHardwareProvider
func makeStripSuffixTransform(suffix string) nameTransformFunc {
	suffixWithUnderscore := "_" + suffix
	pascalSuffix := screamingSnakeToPascal(suffix)

	return func(javaName string) string {
		stripped := strings.TrimSuffix(javaName, suffixWithUnderscore)
		return screamingSnakeToPascal(stripped) + pascalSuffix
	}
}

// screamingSnakeToPascal converts a SCREAMING_SNAKE_CASE identifier to PascalCase.
// For example, "GPS_HARDWARE" becomes "GpsHardware".
func screamingSnakeToPascal(s string) string {
	parts := strings.Split(s, "_")
	var buf strings.Builder
	for _, part := range parts {
		if part == "" {
			continue
		}
		buf.WriteString(strings.ToUpper(part[:1]))
		buf.WriteString(strings.ToLower(part[1:]))
	}
	return buf.String()
}
