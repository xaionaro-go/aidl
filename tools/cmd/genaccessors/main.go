package main

import (
	"encoding/json"
	"flag"
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"sort"
	"strings"

	"github.com/xaionaro-go/binder/tools/pkg/servicemap"
)

func main() {
	serviceMapPath := flag.String("service-map", "", "Path to the service map JSON file")
	outputDir := flag.String("output", ".", "Output directory (project root)")
	flag.Parse()

	if *serviceMapPath == "" {
		fmt.Fprintf(os.Stderr, "Error: -service-map flag is required\n")
		os.Exit(1)
	}

	if err := run(*serviceMapPath, *outputDir); err != nil {
		fmt.Fprintf(os.Stderr, "Error: %v\n", err)
		os.Exit(1)
	}
}

func run(
	serviceMapPath string,
	outputDir string,
) error {
	data, err := os.ReadFile(serviceMapPath)
	if err != nil {
		return fmt.Errorf("reading service map: %w", err)
	}

	var svcMap map[string]servicemap.ServiceMapEntry
	if err := json.Unmarshal(data, &svcMap); err != nil {
		return fmt.Errorf("parsing service map: %w", err)
	}

	keys := make([]string, 0, len(svcMap))
	for k := range svcMap {
		keys = append(keys, k)
	}
	sort.Strings(keys)

	// Track generated accessors to avoid duplicate function names
	// when multiple service keys map to the same AIDL interface.
	generatedAccessors := map[string]bool{}

	generated := 0
	skipped := 0
	for _, key := range keys {
		entry := svcMap[key]
		ok, err := generateAccessor(outputDir, key, entry, generatedAccessors)
		if err != nil {
			return fmt.Errorf("generating accessor for %q: %w", key, err)
		}
		if ok {
			generated++
		} else {
			skipped++
		}
	}

	fmt.Fprintf(os.Stderr, "genaccessors: generated %d accessors, skipped %d entries\n", generated, skipped)
	return nil
}

// generateAccessor generates a single accessor file for the given service map entry.
// Returns true if a file was written, false if the entry was skipped.
// The generatedAccessors map tracks "pkgPath:accessorName" to avoid duplicates
// when multiple service keys map to the same AIDL interface.
func generateAccessor(
	outputDir string,
	serviceKey string,
	entry servicemap.ServiceMapEntry,
	generatedAccessors map[string]bool,
) (bool, error) {
	descriptor := entry.AIDLDescriptor
	// Skip entries with unqualified descriptors (no package path).
	dotIdx := strings.LastIndex(descriptor, ".")
	if dotIdx < 0 {
		return false, nil
	}

	pkgPath := strings.ReplaceAll(descriptor[:dotIdx], ".", "/")
	targetDir := filepath.Join(outputDir, pkgPath)

	// Skip if the package directory does not exist.
	info, err := os.Stat(targetDir)
	if err != nil || !info.IsDir() {
		return false, nil
	}

	interfaceName := entry.AIDLInterface // e.g. "IActivityManager"
	if !strings.HasPrefix(interfaceName, "I") {
		return false, nil
	}

	baseName := interfaceName[1:] // e.g. "ActivityManager"
	proxyType := baseName + "Proxy"
	constructorName := "New" + proxyType
	accessorName := "Get" + baseName
	constantName := screamingSnakeToPascal(entry.ConstantName)

	// Skip if we already generated an accessor with this name in this package.
	dedupKey := pkgPath + ":" + accessorName
	if generatedAccessors[dedupKey] {
		return false, nil
	}

	// Verify the proxy type exists by checking for the constructor in the package files.
	if !proxyConstructorExists(targetDir, constructorName) {
		return false, nil
	}

	// Determine the Go package name (last path component).
	pkgName := filepath.Base(pkgPath)

	src := generateAccessorSource(pkgName, accessorName, proxyType, constructorName, constantName)

	formatted, err := format.Source([]byte(src))
	if err != nil {
		return false, fmt.Errorf("formatting generated source for %s: %w", accessorName, err)
	}

	outputFile := filepath.Join(targetDir, "get_"+serviceKey+".go")
	if err := os.WriteFile(outputFile, formatted, 0o644); err != nil {
		return false, fmt.Errorf("writing %s: %w", outputFile, err)
	}

	generatedAccessors[dedupKey] = true
	return true, nil
}

// proxyConstructorExists checks whether a function named constructorName
// is defined in any .go file within the given directory.
func proxyConstructorExists(
	dir string,
	constructorName string,
) bool {
	entries, err := os.ReadDir(dir)
	if err != nil {
		return false
	}

	needle := "func " + constructorName + "("
	for _, e := range entries {
		if e.IsDir() || !strings.HasSuffix(e.Name(), ".go") {
			continue
		}
		data, err := os.ReadFile(filepath.Join(dir, e.Name()))
		if err != nil {
			continue
		}
		if strings.Contains(string(data), needle) {
			return true
		}
	}
	return false
}

func generateAccessorSource(
	pkgName string,
	accessorName string,
	proxyType string,
	constructorName string,
	constantName string,
) string {
	var buf strings.Builder
	buf.WriteString("// Code generated by genaccessors. DO NOT EDIT.\n\n")
	fmt.Fprintf(&buf, "package %s\n\n", pkgName)
	buf.WriteString("import (\n")
	buf.WriteString("\t\"context\"\n")
	buf.WriteString("\t\"fmt\"\n\n")
	buf.WriteString("\t\"github.com/xaionaro-go/binder/servicemanager\"\n")
	buf.WriteString(")\n\n")
	fmt.Fprintf(&buf, "// %s retrieves the %s service and returns a typed proxy.\n", accessorName, constantName)
	fmt.Fprintf(&buf, "func %s(\n", accessorName)
	buf.WriteString("\tctx context.Context,\n")
	buf.WriteString("\tsm *servicemanager.ServiceManager,\n")
	fmt.Fprintf(&buf, ") (*%s, error) {\n", proxyType)
	fmt.Fprintf(&buf, "\tsvc, err := sm.GetService(ctx, servicemanager.%s)\n", constantName)
	buf.WriteString("\tif err != nil {\n")
	fmt.Fprintf(&buf, "\t\treturn nil, fmt.Errorf(\"%s: %%w\", err)\n", accessorName)
	buf.WriteString("\t}\n")
	fmt.Fprintf(&buf, "\treturn %s(svc), nil\n", constructorName)
	buf.WriteString("}\n")
	return buf.String()
}

// screamingSnakeToPascal converts a SCREAMING_SNAKE_CASE identifier to PascalCase.
// For example, "ACTIVITY_SERVICE" becomes "ActivityService".
func screamingSnakeToPascal(s string) string {
	parts := strings.Split(s, "_")
	var buf strings.Builder
	for _, part := range parts {
		if part == "" {
			continue
		}
		buf.WriteString(strings.ToUpper(part[:1]))
		buf.WriteString(strings.ToLower(part[1:]))
	}
	return buf.String()
}
