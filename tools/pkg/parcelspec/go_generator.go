package parcelspec

import (
	"fmt"
	"go/format"
	"go/token"
	"strconv"
	"strings"
	"unicode"
)

// goTypeInfo describes the Go type and parcel methods for a spec type.
type goTypeInfo struct {
	GoType      string
	WriteMethod string
	ReadMethod  string
}

// specTypeToGo maps spec type strings to their Go type information.
var specTypeToGo = map[string]goTypeInfo{
	"bool":     {GoType: "bool", WriteMethod: "WriteBool", ReadMethod: "ReadBool"},
	"int32":    {GoType: "int32", WriteMethod: "WriteInt32", ReadMethod: "ReadInt32"},
	"int64":    {GoType: "int64", WriteMethod: "WriteInt64", ReadMethod: "ReadInt64"},
	"float32":  {GoType: "float32", WriteMethod: "WriteFloat32", ReadMethod: "ReadFloat32"},
	"float64":  {GoType: "float64", WriteMethod: "WriteFloat64", ReadMethod: "ReadFloat64"},
	"string8":  {GoType: "string", WriteMethod: "WriteString", ReadMethod: "ReadString"},
	"string16": {GoType: "string", WriteMethod: "WriteString16", ReadMethod: "ReadString16"},
}

// GenerateGo generates Go source code implementing MarshalParcel/UnmarshalParcel
// for the given ParcelableSpec. The returned bytes are gofmt-formatted.
func GenerateGo(
	spec ParcelableSpec,
) ([]byte, error) {
	goPkg := goPackageName(spec.Package)

	// Separate supported fields from opaque/invalid fields.
	var supportedFields []FieldSpec
	var opaqueFields []FieldSpec
	for _, f := range spec.Fields {
		if !isValidGoIdentifier(f.Name) {
			opaqueFields = append(opaqueFields, FieldSpec{
				Name: f.Name,
				Type: "opaque",
			})
			continue
		}

		if f.Condition != "" && !isResolvableCondition(f.Condition) {
			// Condition references Java method calls or raw variables
			// that can't be translated to Go.
			opaqueFields = append(opaqueFields, f)
			continue
		}

		switch f.Type {
		case "opaque":
			opaqueFields = append(opaqueFields, f)
		default:
			if _, ok := specTypeToGo[f.Type]; !ok {
				return nil, fmt.Errorf("unknown spec type %q for field %q", f.Type, f.Name)
			}
			supportedFields = append(supportedFields, f)
		}
	}

	// Deduplicate field names by appending a numeric suffix to duplicates.
	supportedFields = deduplicateFieldNames(supportedFields)

	var buf strings.Builder

	buf.WriteString("// Code generated by genparcelgo. DO NOT EDIT.\n\n")
	fmt.Fprintf(&buf, "package %s\n\n", goPkg)
	buf.WriteString("import (\n")
	buf.WriteString("\t\"github.com/xaionaro-go/binder/parcel\"\n")
	buf.WriteString(")\n\n")

	// Struct definition.
	fmt.Fprintf(&buf, "type %s struct {\n", spec.Type)
	for _, f := range supportedFields {
		info := specTypeToGo[f.Type]
		fmt.Fprintf(&buf, "\t%s %s\n", f.Name, info.GoType)
	}
	for _, f := range opaqueFields {
		fmt.Fprintf(&buf, "\t// %s: opaque type — not yet supported\n", f.Name)
	}
	buf.WriteString("}\n\n")

	// Interface assertion.
	fmt.Fprintf(&buf, "var _ parcel.Parcelable = (*%s)(nil)\n\n", spec.Type)

	// MarshalParcel.
	fmt.Fprintf(&buf, "func (s *%s) MarshalParcel(\n", spec.Type)
	buf.WriteString("\tp *parcel.Parcel,\n")
	buf.WriteString(") error {\n")
	for _, f := range supportedFields {
		info := specTypeToGo[f.Type]
		if f.Condition != "" {
			cond := goConditionExpression(f.Condition)
			fmt.Fprintf(&buf, "\tif %s {\n", cond)
			fmt.Fprintf(&buf, "\t\tp.%s(s.%s)\n", info.WriteMethod, f.Name)
			buf.WriteString("\t}\n")
		} else {
			fmt.Fprintf(&buf, "\tp.%s(s.%s)\n", info.WriteMethod, f.Name)
		}
	}
	buf.WriteString("\treturn nil\n")
	buf.WriteString("}\n\n")

	// UnmarshalParcel.
	fmt.Fprintf(&buf, "func (s *%s) UnmarshalParcel(\n", spec.Type)
	buf.WriteString("\tp *parcel.Parcel,\n")
	buf.WriteString(") error {\n")
	if len(supportedFields) > 0 {
		buf.WriteString("\tvar _err error\n")
	}
	for _, f := range supportedFields {
		info := specTypeToGo[f.Type]
		if f.Condition != "" {
			cond := goConditionExpression(f.Condition)
			fmt.Fprintf(&buf, "\tif %s {\n", cond)
			fmt.Fprintf(&buf, "\t\ts.%s, _err = p.%s()\n", f.Name, info.ReadMethod)
			buf.WriteString("\t\tif _err != nil {\n")
			buf.WriteString("\t\t\treturn _err\n")
			buf.WriteString("\t\t}\n")
			buf.WriteString("\t}\n")
		} else {
			fmt.Fprintf(&buf, "\ts.%s, _err = p.%s()\n", f.Name, info.ReadMethod)
			buf.WriteString("\tif _err != nil {\n")
			buf.WriteString("\t\treturn _err\n")
			buf.WriteString("\t}\n")
		}
	}
	buf.WriteString("\treturn nil\n")
	buf.WriteString("}\n")

	return format.Source([]byte(buf.String()))
}

// goPackageName returns the Go package name from a Java package path.
// For example, "android.location" returns "location".
func goPackageName(javaPackage string) string {
	parts := strings.Split(javaPackage, ".")
	return parts[len(parts)-1]
}

// goConditionExpression converts a spec condition like "FieldsMask & 1"
// into a Go boolean expression like "s.FieldsMask & 1 != 0".
func goConditionExpression(condition string) string {
	// Prefix the field reference with "s.".
	parts := strings.SplitN(condition, " & ", 2)
	if len(parts) != 2 {
		return condition
	}

	return fmt.Sprintf("s.%s & %s != 0", parts[0], parts[1])
}

// deduplicateFieldNames appends numeric suffixes to fields with duplicate names.
// For example, if two fields are named "N", they become "N" and "N2".
func deduplicateFieldNames(fields []FieldSpec) []FieldSpec {
	seen := map[string]int{}
	result := make([]FieldSpec, len(fields))
	for i, f := range fields {
		seen[f.Name]++
		count := seen[f.Name]
		if count > 1 {
			f.Name = f.Name + strconv.Itoa(count)
		}
		result[i] = f
	}
	return result
}

// isResolvableCondition checks whether a condition string can be translated
// to valid Go code. Only "FieldName & N" patterns are supported.
// Conditions like "mHasLatitude", "hasExcessPathLength()", etc. reference
// Java methods or variables and cannot be translated.
func isResolvableCondition(condition string) bool {
	parts := strings.SplitN(condition, " & ", 2)
	if len(parts) != 2 {
		return false
	}

	return isValidGoIdentifier(parts[0])
}

// isValidGoIdentifier checks whether the name is a valid Go identifier
// (starts with letter or underscore, followed by letters, digits, or underscores)
// and is not a Go keyword.
func isValidGoIdentifier(name string) bool {
	if len(name) == 0 {
		return false
	}

	for i, r := range name {
		switch {
		case i == 0:
			if !unicode.IsLetter(r) && r != '_' {
				return false
			}
		default:
			if !unicode.IsLetter(r) && !unicode.IsDigit(r) && r != '_' {
				return false
			}
		}
	}

	return !token.IsKeyword(name)
}
