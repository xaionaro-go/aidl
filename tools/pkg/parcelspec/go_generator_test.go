func TestGenerateGo_String16Method(t *testing.T) {
	spec := ParcelableSpec{
		Package: "com.example",
		Type:    "WithString16",
		Fields: []FieldSpec{
			{Name: "Description", Type: "string16"},
		},
	}

	src, err := GenerateGo(spec)
	require.NoError(t, err)

	code := string(src)

	// Verify string16 maps to WriteString16/ReadString16 (not WriteString).
	require.Contains(t, code, "p.WriteString16(s.Description)")
	require.Contains(t, code, "s.Description, _err = p.ReadString16()")

	// Verify no "WriteString8" (which doesn't exist).
	require.True(t, !strings.Contains(code, "String8"))
}
