func TestGenerateGo_BasicNoConditions(t *testing.T) {
	spec := ParcelableSpec{
		Package: "android.location",
		Type:    "LastLocationRequest",
		Fields: []FieldSpec{
			{Name: "HiddenFromAppOps", Type: "bool"},
			{Name: "AdasGnssBypass", Type: "bool"},
			{Name: "LocationSettingsIgnored", Type: "bool"},
		},
	}

	src, err := GenerateGo(spec)
	require.NoError(t, err)

	code := string(src)

	// Verify package declaration.
	require.Contains(t, code, "package location")

	// Verify generated header comment.
	require.Contains(t, code, "// Code generated by genparcelgo. DO NOT EDIT.")

	// Verify struct fields.
	require.Contains(t, code, "HiddenFromAppOps")
	require.Contains(t, code, "AdasGnssBypass")
	require.Contains(t, code, "LocationSettingsIgnored")

	// Verify interface assertion.
	require.Contains(t, code, "var _ parcel.Parcelable = (*LastLocationRequest)(nil)")

	// Verify marshal calls.
	require.Contains(t, code, "p.WriteBool(s.HiddenFromAppOps)")
	require.Contains(t, code, "p.WriteBool(s.AdasGnssBypass)")
	require.Contains(t, code, "p.WriteBool(s.LocationSettingsIgnored)")

	// Verify unmarshal calls.
	require.Contains(t, code, "s.HiddenFromAppOps, _err = p.ReadBool()")
	require.Contains(t, code, "s.AdasGnssBypass, _err = p.ReadBool()")
	require.Contains(t, code, "s.LocationSettingsIgnored, _err = p.ReadBool()")

	// Verify no conditional blocks.
	require.NotContains(t, code, "s.FieldsMask &")

	// Verify the generated code parses as valid Go.
	fset := token.NewFileSet()
	_, err = parser.ParseFile(fset, "lastlocationrequest.go", src, parser.AllErrors)
	require.NoError(t, err)
}

func TestGenerateGo_ConditionalFields(t *testing.T) {
	spec := ParcelableSpec{
		Package: "android.location",
		Type:    "Location",
		Fields: []FieldSpec{
			{Name: "Provider", Type: "string8"},
			{Name: "FieldsMask", Type: "int32"},
			{Name: "TimeMs", Type: "int64"},
			{Name: "AltitudeMeters", Type: "float64", Condition: "FieldsMask & 1"},
			{Name: "SpeedMetersPerSecond", Type: "float32", Condition: "FieldsMask & 2"},
		},
	}

	src, err := GenerateGo(spec)
	require.NoError(t, err)

	code := string(src)

	// Verify struct fields exist (gofmt may align them with extra whitespace).
	require.Contains(t, code, "Provider")
	require.Contains(t, code, "FieldsMask")
	require.Contains(t, code, "TimeMs")
	require.Contains(t, code, "AltitudeMeters")
	require.Contains(t, code, "SpeedMetersPerSecond")

	// Verify unconditional writes.
	require.Contains(t, code, "p.WriteString(s.Provider)")
	require.Contains(t, code, "p.WriteInt32(s.FieldsMask)")
	require.Contains(t, code, "p.WriteInt64(s.TimeMs)")

	// Verify conditional marshal blocks (gofmt removes spaces around &).
	require.Contains(t, code, "if s.FieldsMask&1 != 0 {")
	require.Contains(t, code, "p.WriteFloat64(s.AltitudeMeters)")
	require.Contains(t, code, "if s.FieldsMask&2 != 0 {")
	require.Contains(t, code, "p.WriteFloat32(s.SpeedMetersPerSecond)")

	// Verify conditional unmarshal blocks.
	require.Contains(t, code, "s.AltitudeMeters, _err = p.ReadFloat64()")
	require.Contains(t, code, "s.SpeedMetersPerSecond, _err = p.ReadFloat32()")

	// Verify the generated code parses as valid Go.
	fset := token.NewFileSet()
	_, err = parser.ParseFile(fset, "location.go", src, parser.AllErrors)
	require.NoError(t, err)
}

