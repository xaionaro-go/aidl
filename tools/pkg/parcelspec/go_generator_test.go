func TestGenerateGo_AllTypes(t *testing.T) {
	spec := ParcelableSpec{
		Package: "com.example",
		Type:    "AllTypes",
		Fields: []FieldSpec{
			{Name: "B", Type: "bool"},
			{Name: "I32", Type: "int32"},
			{Name: "I64", Type: "int64"},
			{Name: "F32", Type: "float32"},
			{Name: "F64", Type: "float64"},
			{Name: "S8", Type: "string8"},
			{Name: "S16", Type: "string16"},
		},
	}

	src, err := GenerateGo(spec)
	require.NoError(t, err)

	code := string(src)

	// Verify package name.
	require.Contains(t, code, "package example")

	// Verify all write methods.
	require.Contains(t, code, "p.WriteBool(s.B)")
	require.Contains(t, code, "p.WriteInt32(s.I32)")
	require.Contains(t, code, "p.WriteInt64(s.I64)")
	require.Contains(t, code, "p.WriteFloat32(s.F32)")
	require.Contains(t, code, "p.WriteFloat64(s.F64)")
	require.Contains(t, code, "p.WriteString(s.S8)")
	require.Contains(t, code, "p.WriteString16(s.S16)")

	// Verify all read methods.
	require.Contains(t, code, "s.B, _err = p.ReadBool()")
	require.Contains(t, code, "s.I32, _err = p.ReadInt32()")
	require.Contains(t, code, "s.I64, _err = p.ReadInt64()")
	require.Contains(t, code, "s.F32, _err = p.ReadFloat32()")
	require.Contains(t, code, "s.F64, _err = p.ReadFloat64()")
	require.Contains(t, code, "s.S8, _err = p.ReadString()")
	require.Contains(t, code, "s.S16, _err = p.ReadString16()")

	// Verify valid Go.
	fset := token.NewFileSet()
	_, err = parser.ParseFile(fset, "alltypes.go", src, parser.AllErrors)
	require.NoError(t, err)
}

