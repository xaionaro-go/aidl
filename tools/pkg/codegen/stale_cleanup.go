package codegen

import (
	"bufio"
	"os"
	"path/filepath"
	"strings"
)

// generatedFileMarker is the marker string that appears in the first few
// lines of generated .go files. Only files containing this marker are
// considered for stale file removal.
const generatedFileMarker = "Code generated by aidlgen"

// removeStaleGeneratedFiles scans dir for .go files that contain the
// generated-code marker but are not in the expectedFiles set, and removes
// them. It also removes stale subdirectories left by the old generator
// that created separate Go packages for nested AIDL types (e.g.,
// ISoundDose/IHalSoundDoseCallback/). The current generator flattens
// nested types into the parent package using dotted file names.
//
// Stale files and subdirectories can introduce import cycles when they
// reference cross-package types that create circular dependencies.
func removeStaleGeneratedFiles(
	dir string,
	expectedFiles map[string]bool,
	generatedDirs map[string]bool,
) {
	entries, err := os.ReadDir(dir)
	if err != nil {
		return
	}

	for _, entry := range entries {
		path := filepath.Join(dir, entry.Name())

		// Handle stale subdirectories: the old generator created
		// subdirectories for nested AIDL types (e.g., ISoundDose/).
		// If a subdirectory is not in the set of expected output
		// directories and contains only generated files, remove it.
		if entry.IsDir() {
			if !generatedDirs[path] {
				removeStaleGeneratedDir(path)
			}
			continue
		}

		name := entry.Name()
		if !strings.HasSuffix(name, ".go") {
			continue
		}
		// Never remove test files -- they are managed separately.
		if strings.HasSuffix(name, "_test.go") {
			continue
		}

		if expectedFiles[path] {
			continue
		}

		if isGeneratedFile(path) {
			_ = os.Remove(path)
		}
	}
}

// removeStaleGeneratedDir recursively removes a directory tree if it
// contains only generated .go files and subdirectories (no user-written
// files). Returns true if the directory was removed.
func removeStaleGeneratedDir(dir string) bool {
	entries, err := os.ReadDir(dir)
	if err != nil {
		return false
	}

	for _, entry := range entries {
		path := filepath.Join(dir, entry.Name())

		if entry.IsDir() {
			if !removeStaleGeneratedDir(path) {
				return false
			}
			continue
		}

		name := entry.Name()
		if !strings.HasSuffix(name, ".go") {
			// Non-Go file present: not safe to remove.
			return false
		}
		// Test files are excluded from removal.
		if strings.HasSuffix(name, "_test.go") {
			return false
		}

		if !isGeneratedFile(path) {
			return false
		}
	}

	// All contents are generated. Remove the entire directory.
	_ = os.RemoveAll(dir)
	return true
}

// isGeneratedFile returns true if the file contains the generated-code
// marker within its first 5 lines. Limiting the scan avoids reading
// entire files.
func isGeneratedFile(path string) bool {
	f, err := os.Open(path)
	if err != nil {
		return false
	}
	defer f.Close()

	scanner := bufio.NewScanner(f)
	for i := 0; i < 5 && scanner.Scan(); i++ {
		if strings.Contains(scanner.Text(), generatedFileMarker) {
			return true
		}
	}
	return false
}
