package codegen

import (
	"sort"

	"github.com/xaionaro-go/binder/tools/pkg/parser"
)

// GenerateSmokeTests generates a smoke_test.go file containing one test
// function per interface proxy. All interfaces in the same Go package
// are grouped into a single test file.
func GenerateSmokeTests(
	interfaces []*parser.InterfaceDecl,
	goPackage string,
) ([]byte, error) {
	f := NewGoFile(goPackage)
	f.AddImport("testing", "")
	f.AddImport("github.com/xaionaro-go/binder/tools/pkg/testutil", "")

	f.P("// Code generated by aospgen. DO NOT EDIT.")
	f.P("")

	// Sort interfaces by name for deterministic output.
	sorted := make([]*parser.InterfaceDecl, len(interfaces))
	copy(sorted, interfaces)
	sort.Slice(sorted, func(i, j int) bool {
		return sorted[i].IntfName < sorted[j].IntfName
	})

	for _, decl := range sorted {
		interfaceName := AIDLToGoName(decl.IntfName)
		proxyName := deriveProxyName(interfaceName)
		writeSmokeTestFunc(f, proxyName)
	}

	return f.Bytes()
}

// writeSmokeTestFunc writes a single TestSmoke_<ProxyName> function.
func writeSmokeTestFunc(
	f *GoFile,
	proxyName string,
) {
	f.P("func TestSmoke_%s(t *testing.T) {", proxyName)
	f.P("\tmock := testutil.NewMockBinder()")
	f.P("\tproxy := New%s(mock)", proxyName)
	f.P("\tresult := testutil.SmokeTestAllMethods(t, proxy)")
	f.P("\tt.Logf(\"%s: %%d/%%d passed, %%d panicked, %%d failed\",", proxyName)
	f.P("\t\tresult.Passed, result.Total, result.Panicked, result.Failed)")
	f.P("\tif result.Failed > 0 {")
	f.P("\t\tt.Errorf(\"%%d methods failed unexpectedly\", result.Failed)")
	f.P("\t}")
	f.P("}")
	f.P("")
}
