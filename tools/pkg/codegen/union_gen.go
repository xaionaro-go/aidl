package codegen

import (
	"fmt"
	"strings"

	"github.com/xaionaro-go/binder/tools/pkg/parser"
)

// GenerateUnion generates Go source for an AIDL union declaration.
// The output includes:
// - A Go struct with a Tag field and one field per variant
// - Tag constants for each variant (unless a separate Tag enum exists)
// - Accessor methods for each variant (Get and Set)
// - MarshalParcel and UnmarshalParcel methods
func GenerateUnion(
	decl *parser.UnionDecl,
	pkg string,
	qualifiedName string,
	options ...GenOption,
) ([]byte, error) {
	opts := applyGenOptions(options)
	f := NewGoFile(pkg)
	typeRef := opts.newTypeRefResolver(f)
	f.AddImport("fmt", "")
	f.AddImport("github.com/xaionaro-go/binder/parcel", "")

	// Add binder import if any field is an interface or IBinder type.
	if hasBinderField(decl.Fields, opts, typeRef) {
		f.AddImport("github.com/xaionaro-go/binder/binder", "")
	}

	structName := AIDLToGoName(decl.UnionName)
	recFields := opts.recursiveFields()

	// Check if a separate Tag enum exists in the registry (e.g.,
	// "AcousticEchoCanceler.Tag"). When present, the enum file
	// provides the tag type and constants, so the union must use
	// the named enum type instead of int32 and skip inline constants.
	tagGoType := "int32"
	hasTagEnum := lookupTagEnum(qualifiedName, opts)
	if hasTagEnum {
		tagGoType = structName + "Tag"
	}

	f.P("// Code generated by aidlgen. DO NOT EDIT.")
	f.P("")

	// Tag constants: only generate inline when no separate Tag enum
	// exists in the registry. When the enum exists, it is generated
	// in its own file and would conflict with inline declarations.
	if !hasTagEnum {
		f.P("const (")
		for i, field := range decl.Fields {
			tagName := structName + "Tag" + AIDLToGoName(field.FieldName)
			f.P("\t%s int32 = %d", tagName, i)
		}
		f.P(")")
		f.P("")
	}

	// Struct declaration with tag and variant fields.
	// Fields whose types create recursive struct definitions use pointer
	// types to break the infinite-size cycle.
	f.P("type %s struct {", structName)
	f.P("\tTag %s", tagGoType)
	for _, field := range decl.Fields {
		goType := resolveTypeRef(typeRef, field.Type)
		switch {
		case isNullableParcelableField(field, opts, typeRef):
			goType = "*" + goType
		case recFields.needsPointer(structName, goType):
			goType = "*" + goType
		}
		goFieldName := AIDLToGoName(field.FieldName)
		f.P("\t%s %s", goFieldName, goType)
	}
	f.P("}")
	f.P("")

	// Constants declared inside the union.
	if len(decl.Constants) > 0 {
		if err := GenerateConstants(decl.Constants, f, structName); err != nil {
			return nil, fmt.Errorf("generating union constants: %w", err)
		}
		f.P("")
	}

	// Parcelable interface compliance.
	f.P("var _ parcel.Parcelable = (*%s)(nil)", structName)
	f.P("")

	// Accessor methods.
	for i, field := range decl.Fields {
		writeUnionAccessors(f, structName, field, i, typeRef, recFields, opts)
	}

	// MarshalParcel method.
	writeUnionMarshalParcel(f, structName, decl.Fields, opts, typeRef, recFields, hasTagEnum)

	// UnmarshalParcel method.
	writeUnionUnmarshalParcel(f, structName, decl.Fields, opts, typeRef, recFields, hasTagEnum)

	return f.Bytes()
}

// writeUnionAccessors writes Get and Set methods for a union variant.
func writeUnionAccessors(
	f *GoFile,
	structName string,
	field *parser.FieldDecl,
	index int,
	typeRef *TypeRefResolver,
	recFields *recursiveFieldSet,
	opts GenOptions,
) {
	goFieldName := AIDLToGoName(field.FieldName)
	goType := resolveTypeRef(typeRef, field.Type)
	isRecPtr := recFields.needsPointer(structName, goType)
	isNullableParc := isNullableParcelableField(field, opts, typeRef)
	tagConst := structName + "Tag" + goFieldName

	// When the field is a pointer (to break recursive types or @nullable
	// parcelable), the accessor signatures use the pointer type to match
	// the struct field.
	accessorType := goType
	if isNullableParc || isRecPtr {
		accessorType = "*" + goType
	}

	// Getter.
	f.P("func (u *%s) Get%s() (%s, bool) {", structName, goFieldName, accessorType)
	f.P("\tif u.Tag != %s {", tagConst)
	f.P("\t\tvar _zero %s", accessorType)
	f.P("\t\treturn _zero, false")
	f.P("\t}")
	f.P("\treturn u.%s, true", goFieldName)
	f.P("}")
	f.P("")

	// Setter: replace the entire struct to zero previous variant fields.
	f.P("func (u *%s) Set%s(", structName, goFieldName)
	f.P("\tv %s,", accessorType)
	f.P(") {")
	f.P("\t*u = %s{Tag: %s, %s: v}", structName, tagConst, goFieldName)
	f.P("}")
	f.P("")
}

// writeUnionMarshalParcel generates the MarshalParcel method for a union.
func writeUnionMarshalParcel(
	f *GoFile,
	structName string,
	fields []*parser.FieldDecl,
	opts GenOptions,
	typeRef *TypeRefResolver,
	recFields *recursiveFieldSet,
	hasTagEnum bool,
) {
	f.P("func (u *%s) MarshalParcel(", structName)
	f.P("\tp *parcel.Parcel,")
	f.P(") error {")
	f.P("\t_headerPos := parcel.WriteParcelableHeader(p)")
	if hasTagEnum {
		f.P("\tp.WriteInt32(int32(u.Tag))")
	} else {
		f.P("\tp.WriteInt32(u.Tag)")
	}
	f.P("")
	f.P("\tswitch u.Tag {")

	for _, field := range fields {
		goFieldName := AIDLToGoName(field.FieldName)
		tagConst := structName + "Tag" + goFieldName
		fieldAccess := "u." + goFieldName
		// Merge field-level annotations (e.g. @utf8InCpp) into the type
		// so marshalForType sees them when choosing the write method.
		fieldType := fieldTypeWithAnnotations(field)

		f.P("\tcase %s:", tagConst)

		if fieldType.IsArray || fieldType.Name == "List" {
			writeArrayFieldToParcel(f, fieldType, fieldAccess, "p", structName, opts, typeRef)
			continue
		}

		if fieldType.Name == "Map" {
			writeMapFieldToParcel(f, fieldType, fieldAccess, "p", opts, typeRef)
			continue
		}

		info := marshalForTypeWithCycleCheck(fieldType, opts, typeRef)
		if info.WriteExpr == "" {
			continue
		}

		goType := resolveTypeRef(typeRef, field.Type)
		isRecPtr := recFields.needsPointer(structName, goType)
		isNullableParc := isNullableParcelableField(field, opts, typeRef)

		if isMarshalParcelWrite(info.WriteExpr) {
			if isRecPtr || isNullableParc {
				// Pointer field (recursive type): write null indicator
				// then marshal if non-nil.
				f.P("\t\tif %s == nil {", fieldAccess)
				f.P("\t\t\tp.WriteInt32(0)")
				f.P("\t\t} else {")
				f.P("\t\t\tp.WriteInt32(1)")
				f.P("\t\t\tif _err := %s.MarshalParcel(p); _err != nil {", fieldAccess)
				f.P("\t\t\t\treturn _err")
				f.P("\t\t\t}")
				f.P("\t\t}")
			} else {
				// NDK AIDL wire format: int32(1) non-null indicator before
				// parcelable variant, matching writeTypedObject semantics.
				f.P("\t\tp.WriteInt32(1)")
				f.P("\t\tif _err := %s.MarshalParcel(p); _err != nil {", fieldAccess)
				f.P("\t\t\treturn _err")
				f.P("\t\t}")
			}
		} else {
			writeExpr := fmt.Sprintf(info.WriteExpr, fieldAccess)
			writeExpr = replaceParcelVar(writeExpr, "_data", "p")
			f.P("\t\t%s", writeExpr)
		}
	}

	f.P("\tdefault:")
	f.P("\t\treturn fmt.Errorf(\"unknown union tag %%d for %s\", u.Tag)", structName)
	f.P("\t}")
	f.P("")
	f.P("\tparcel.WriteParcelableFooter(p, _headerPos)")
	f.P("\treturn nil")
	f.P("}")
	f.P("")
}

// writeUnionUnmarshalParcel generates the UnmarshalParcel method for a union.
func writeUnionUnmarshalParcel(
	f *GoFile,
	structName string,
	fields []*parser.FieldDecl,
	opts GenOptions,
	typeRef *TypeRefResolver,
	recFields *recursiveFieldSet,
	hasTagEnum bool,
) {
	tagGoType := structName + "Tag"
	f.P("func (u *%s) UnmarshalParcel(", structName)
	f.P("\tp *parcel.Parcel,")
	f.P(") error {")
	f.P("\t_endPos, _err := parcel.ReadParcelableHeader(p)")
	f.P("\tif _err != nil {")
	f.P("\t\treturn _err")
	f.P("\t}")
	f.P("")
	if hasTagEnum {
		// Read int32 from the wire and convert to the named Tag type.
		f.P("\t_tagRaw, _err := p.ReadInt32()")
		f.P("\tif _err != nil {")
		f.P("\t\treturn _err")
		f.P("\t}")
		f.P("\tu.Tag = %s(_tagRaw)", tagGoType)
	} else {
		f.P("\tu.Tag, _err = p.ReadInt32()")
		f.P("\tif _err != nil {")
		f.P("\t\treturn _err")
		f.P("\t}")
	}
	f.P("")
	f.P("\tswitch u.Tag {")

	arrayIdx := 0
	mapIdx := 0
	for _, field := range fields {
		goFieldName := AIDLToGoName(field.FieldName)
		tagConst := structName + "Tag" + goFieldName
		fieldAccess := "u." + goFieldName
		// Merge field-level annotations (e.g. @utf8InCpp) into the type
		// so marshalForType sees them when choosing the read method.
		fieldType := fieldTypeWithAnnotations(field)

		f.P("\tcase %s:", tagConst)

		if fieldType.IsArray || fieldType.Name == "List" {
			readArrayFieldFromParcel(f, fieldType, fieldAccess, "p", arrayIdx, structName, opts, typeRef)
			arrayIdx++
			continue
		}

		if fieldType.Name == "Map" {
			readMapFieldFromParcel(f, fieldType, fieldAccess, "p", mapIdx, opts, typeRef)
			mapIdx++
			continue
		}

		info := marshalForTypeWithCycleCheck(fieldType, opts, typeRef)
		if info.ReadExpr == "" {
			continue
		}

		goType := resolveTypeRef(typeRef, field.Type)
		isRecPtr := recFields.needsPointer(structName, goType)
		isNullableParc := isNullableParcelableField(field, opts, typeRef)

		readExpr := replaceParcelVar(info.ReadExpr, "_reply", "p")

		switch {
		case isUnmarshalParcelRead(info.ReadExpr):
			if isRecPtr || isNullableParc {
				// Pointer field (recursive type): read null indicator,
				// allocate and unmarshal if non-null.
				f.P("\t\t{")
				f.P("\t\t\t_nullInd, _nullErr := p.ReadInt32()")
				f.P("\t\t\tif _nullErr != nil {")
				f.P("\t\t\t\treturn _nullErr")
				f.P("\t\t\t}")
				f.P("\t\t\tif _nullInd != 0 {")
				f.P("\t\t\t\tvar _val %s", goType)
				f.P("\t\t\t\tif _err = _val.UnmarshalParcel(p); _err != nil {")
				f.P("\t\t\t\t\treturn _err")
				f.P("\t\t\t\t}")
				f.P("\t\t\t\tu.%s = &_val", goFieldName)
				f.P("\t\t\t}")
				f.P("\t\t}")
			} else {
				// NDK AIDL wire format: int32 non-null indicator before
				// parcelable variant, matching readTypedObject semantics.
				f.P("\t\tif _, _err = p.ReadInt32(); _err != nil {")
				f.P("\t\t\treturn _err")
				f.P("\t\t}")
				f.P("\t\tif _err = u.%s.UnmarshalParcel(p); _err != nil {", goFieldName)
				f.P("\t\t\treturn _err")
				f.P("\t\t}")
			}
		case info.IsInterface:
			proxyConstructor := interfaceProxyConstructor(typeRef, field.Type)
			f.P("\t\t_handle, _err := %s", readExpr)
			f.P("\t\tif _err != nil {")
			f.P("\t\t\treturn _err")
			f.P("\t\t}")
			f.P("\t\tu.%s = %s(binder.NewProxyBinder(nil, binder.CallerIdentity{}, _handle))", goFieldName, proxyConstructor)
		case info.IsIBinder:
			f.P("\t\t_handle, _err := %s", readExpr)
			f.P("\t\tif _err != nil {")
			f.P("\t\t\treturn _err")
			f.P("\t\t}")
			// Null binder handles are represented as 0; leave field nil.
			f.P("\t\tif _handle != 0 {")
			f.P("\t\t\tu.%s = binder.NewProxyBinder(nil, binder.CallerIdentity{}, _handle)", goFieldName)
			f.P("\t\t}")
		case info.NeedsCast:
			f.P("\t\t_raw, _err := %s", readExpr)
			f.P("\t\tif _err != nil {")
			f.P("\t\t\treturn _err")
			f.P("\t\t}")
			f.P("\t\tu.%s = %s(_raw)", goFieldName, goType)
		default:
			f.P("\t\tu.%s, _err = %s", goFieldName, readExpr)
			f.P("\t\tif _err != nil {")
			f.P("\t\t\treturn _err")
			f.P("\t\t}")
		}
	}

	f.P("\tdefault:")
	f.P("\t\treturn fmt.Errorf(\"unknown union tag %%d for %s\", u.Tag)", structName)
	f.P("\t}")
	f.P("")
	f.P("\tparcel.SkipToParcelableEnd(p, _endPos)")
	f.P("\treturn nil")
	f.P("}")
	f.P("")
}

// replaceParcelVar replaces _data. or _reply. prefixes with a target variable.
func replaceParcelVar(
	expr string,
	from string,
	to string,
) string {
	old := from + "."
	new := to + "."
	result := expr
	for i := 0; i < len(result); i++ {
		if i+len(old) <= len(result) && result[i:i+len(old)] == old {
			result = result[:i] + new + result[i+len(old):]
			i += len(new) - 1
		}
	}
	return result
}

// isMarshalParcelWrite returns true if the write expression is a MarshalParcel call.
func isMarshalParcelWrite(writeExpr string) bool {
	return strings.Contains(writeExpr, ".MarshalParcel")
}

// isUnmarshalParcelRead returns true if the read expression is an UnmarshalParcel call.
func isUnmarshalParcelRead(readExpr string) bool {
	return strings.Contains(readExpr, ".UnmarshalParcel")
}

// lookupTagEnum checks whether a Tag enum (e.g., "AcousticEchoCanceler.Tag")
// exists in the registry for the given union's qualified name. When present,
// the enum provides the tag type and constants, so the union codegen should
// use the named enum type instead of int32 and skip inline constant generation.
func lookupTagEnum(
	unionQualifiedName string,
	opts GenOptions,
) bool {
	if opts.Registry == nil {
		return false
	}
	tagQN := unionQualifiedName + ".Tag"
	def, ok := opts.Registry.Lookup(tagQN)
	if !ok {
		return false
	}
	_, isEnum := def.(*parser.EnumDecl)
	return isEnum
}
