package codegen

import (
	"fmt"
	"strings"

	"github.com/xaionaro-go/binder/tools/pkg/parser"
)

// GenerateUnion generates Go source for an AIDL union declaration.
// The output includes:
// - A Go struct with a Tag field and one field per variant
// - Tag constants for each variant
// - Accessor methods for each variant (Get and Set)
// - MarshalParcel and UnmarshalParcel methods
func GenerateUnion(
	decl *parser.UnionDecl,
	pkg string,
	qualifiedName string,
	options ...GenOption,
) ([]byte, error) {
	opts := applyGenOptions(options)
	f := NewGoFile(pkg)
	typeRef := opts.newTypeRefResolver(f)
	f.AddImport("fmt", "")
	f.AddImport("github.com/xaionaro-go/binder/parcel", "")

	// Add binder import if any field is an interface or IBinder type.
	if hasBinderField(decl.Fields, opts, typeRef) {
		f.AddImport("github.com/xaionaro-go/binder/binder", "")
	}

	structName := AIDLToGoName(decl.UnionName)

	f.P("// Code generated by aidlgen. DO NOT EDIT.")
	f.P("")

	// Tag constants.
	f.P("const (")
	for i, field := range decl.Fields {
		tagName := structName + "Tag" + AIDLToGoName(field.FieldName)
		f.P("\t%s int32 = %d", tagName, i)
	}
	f.P(")")
	f.P("")

	// Struct declaration with tag and variant fields.
	f.P("type %s struct {", structName)
	f.P("\tTag int32")
	for _, field := range decl.Fields {
		goType := resolveTypeRef(typeRef, field.Type)
		goFieldName := AIDLToGoName(field.FieldName)
		f.P("\t%s %s", goFieldName, goType)
	}
	f.P("}")
	f.P("")

	// Constants declared inside the union.
	if len(decl.Constants) > 0 {
		if err := GenerateConstants(decl.Constants, f, structName); err != nil {
			return nil, fmt.Errorf("generating union constants: %w", err)
		}
		f.P("")
	}

	// Parcelable interface compliance.
	f.P("var _ parcel.Parcelable = (*%s)(nil)", structName)
	f.P("")

	// Accessor methods.
	for i, field := range decl.Fields {
		writeUnionAccessors(f, structName, field, i, typeRef)
	}

	// MarshalParcel method.
	writeUnionMarshalParcel(f, structName, decl.Fields, opts, typeRef)

	// UnmarshalParcel method.
	writeUnionUnmarshalParcel(f, structName, decl.Fields, opts, typeRef)

	return f.Bytes()
}

// writeUnionAccessors writes Get and Set methods for a union variant.
func writeUnionAccessors(
	f *GoFile,
	structName string,
	field *parser.FieldDecl,
	index int,
	typeRef *TypeRefResolver,
) {
	goFieldName := AIDLToGoName(field.FieldName)
	goType := resolveTypeRef(typeRef, field.Type)
	tagConst := structName + "Tag" + goFieldName

	// Getter.
	f.P("func (u *%s) Get%s() (%s, bool) {", structName, goFieldName, goType)
	f.P("\tif u.Tag != %s {", tagConst)
	f.P("\t\tvar _zero %s", goType)
	f.P("\t\treturn _zero, false")
	f.P("\t}")
	f.P("\treturn u.%s, true", goFieldName)
	f.P("}")
	f.P("")

	// Setter.
	f.P("func (u *%s) Set%s(", structName, goFieldName)
	f.P("\tv %s,", goType)
	f.P(") {")
	f.P("\tu.Tag = %s", tagConst)
	f.P("\tu.%s = v", goFieldName)
	f.P("}")
	f.P("")
}

// writeUnionMarshalParcel generates the MarshalParcel method for a union.
func writeUnionMarshalParcel(
	f *GoFile,
	structName string,
	fields []*parser.FieldDecl,
	opts GenOptions,
	typeRef *TypeRefResolver,
) {
	f.P("func (u *%s) MarshalParcel(", structName)
	f.P("\tp *parcel.Parcel,")
	f.P(") error {")
	f.P("\t_headerPos := parcel.WriteParcelableHeader(p)")
	f.P("\tp.WriteInt32(u.Tag)")
	f.P("")
	f.P("\tswitch u.Tag {")

	for _, field := range fields {
		goFieldName := AIDLToGoName(field.FieldName)
		tagConst := structName + "Tag" + goFieldName
		fieldAccess := "u." + goFieldName

		f.P("\tcase %s:", tagConst)

		if field.Type.IsArray || field.Type.Name == "List" {
			writeArrayFieldToParcel(f, field.Type, fieldAccess, "p", opts, typeRef)
			continue
		}

		if field.Type.Name == "Map" {
			writeMapFieldToParcel(f, field.Type, fieldAccess, "p", opts, typeRef)
			continue
		}

		info := marshalForTypeWithCycleCheck(field.Type, opts, typeRef)
		if info.WriteExpr != "" {
			if isMarshalParcelWrite(info.WriteExpr) {
				f.P("\t\tif _err := %s.MarshalParcel(p); _err != nil {", fieldAccess)
				f.P("\t\t\treturn _err")
				f.P("\t\t}")
			} else {
				writeExpr := fmt.Sprintf(info.WriteExpr, fieldAccess)
				writeExpr = replaceParcelVar(writeExpr, "_data", "p")
				f.P("\t\t%s", writeExpr)
			}
		}
	}

	f.P("\tdefault:")
	f.P("\t\treturn fmt.Errorf(\"unknown union tag %%d for %s\", u.Tag)", structName)
	f.P("\t}")
	f.P("")
	f.P("\tparcel.WriteParcelableFooter(p, _headerPos)")
	f.P("\treturn nil")
	f.P("}")
	f.P("")
}

// writeUnionUnmarshalParcel generates the UnmarshalParcel method for a union.
func writeUnionUnmarshalParcel(
	f *GoFile,
	structName string,
	fields []*parser.FieldDecl,
	opts GenOptions,
	typeRef *TypeRefResolver,
) {
	f.P("func (u *%s) UnmarshalParcel(", structName)
	f.P("\tp *parcel.Parcel,")
	f.P(") error {")
	f.P("\t_endPos, _err := parcel.ReadParcelableHeader(p)")
	f.P("\tif _err != nil {")
	f.P("\t\treturn _err")
	f.P("\t}")
	f.P("")
	f.P("\tu.Tag, _err = p.ReadInt32()")
	f.P("\tif _err != nil {")
	f.P("\t\treturn _err")
	f.P("\t}")
	f.P("")
	f.P("\tswitch u.Tag {")

	arrayIdx := 0
	for _, field := range fields {
		goFieldName := AIDLToGoName(field.FieldName)
		tagConst := structName + "Tag" + goFieldName
		fieldAccess := "u." + goFieldName

		f.P("\tcase %s:", tagConst)

		if field.Type.IsArray || field.Type.Name == "List" {
			readArrayFieldFromParcel(f, field.Type, fieldAccess, "p", arrayIdx, opts, typeRef)
			arrayIdx++
			continue
		}

		if field.Type.Name == "Map" {
			readMapFieldFromParcel(f, field.Type, fieldAccess, "p", opts, typeRef)
			continue
		}

		info := marshalForTypeWithCycleCheck(field.Type, opts, typeRef)
		if info.ReadExpr == "" {
			continue
		}

		readExpr := replaceParcelVar(info.ReadExpr, "_reply", "p")

		switch {
		case isUnmarshalParcelRead(info.ReadExpr):
			f.P("\t\tif _err = u.%s.UnmarshalParcel(p); _err != nil {", goFieldName)
			f.P("\t\t\treturn _err")
			f.P("\t\t}")
		case info.IsInterface:
			proxyConstructor := interfaceProxyConstructor(typeRef, field.Type)
			f.P("\t\t_handle, _err := %s", readExpr)
			f.P("\t\tif _err != nil {")
			f.P("\t\t\treturn _err")
			f.P("\t\t}")
			f.P("\t\tu.%s = %s(binder.NewProxyBinder(nil, _handle))", goFieldName, proxyConstructor)
		case info.IsIBinder:
			f.P("\t\t_handle, _err := %s", readExpr)
			f.P("\t\tif _err != nil {")
			f.P("\t\t\treturn _err")
			f.P("\t\t}")
			f.P("\t\tu.%s = binder.NewProxyBinder(nil, _handle)", goFieldName)
		case info.NeedsCast:
			goType := resolveTypeRef(typeRef, field.Type)
			f.P("\t\t_raw, _err := %s", readExpr)
			f.P("\t\tif _err != nil {")
			f.P("\t\t\treturn _err")
			f.P("\t\t}")
			f.P("\t\tu.%s = %s(_raw)", goFieldName, goType)
		default:
			f.P("\t\tu.%s, _err = %s", goFieldName, readExpr)
			f.P("\t\tif _err != nil {")
			f.P("\t\t\treturn _err")
			f.P("\t\t}")
		}
	}

	f.P("\tdefault:")
	f.P("\t\treturn fmt.Errorf(\"unknown union tag %%d for %s\", u.Tag)", structName)
	f.P("\t}")
	f.P("")
	f.P("\tparcel.SkipToParcelableEnd(p, _endPos)")
	f.P("\treturn nil")
	f.P("}")
	f.P("")
}

// replaceParcelVar replaces _data. or _reply. prefixes with a target variable.
func replaceParcelVar(
	expr string,
	from string,
	to string,
) string {
	old := from + "."
	new := to + "."
	result := expr
	for i := 0; i < len(result); i++ {
		if i+len(old) <= len(result) && result[i:i+len(old)] == old {
			result = result[:i] + new + result[i+len(old):]
			i += len(new) - 1
		}
	}
	return result
}

// isMarshalParcelWrite returns true if the write expression is a MarshalParcel call.
func isMarshalParcelWrite(writeExpr string) bool {
	return strings.Contains(writeExpr, ".MarshalParcel")
}

// isUnmarshalParcelRead returns true if the read expression is an UnmarshalParcel call.
func isUnmarshalParcelRead(readExpr string) bool {
	return strings.Contains(readExpr, ".UnmarshalParcel")
}
