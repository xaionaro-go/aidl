// GenerateInterface generates Go source for an AIDL interface declaration.
// The output includes:
// - A Go interface type with all methods
// - A client-side proxy struct
// - A constructor for the proxy
// - Proxy method implementations that serialize/deserialize via parcels
// - Transaction code constants
// - A descriptor constant
func GenerateInterface(
	decl *parser.InterfaceDecl,
	pkg string,
	qualifiedName string,
	options ...GenOption,
) ([]byte, error) {
	opts := applyGenOptions(options)
	f := NewGoFile(pkg)
	typeRef := opts.newTypeRefResolver(f)

	// Reserve all parameter names so that import aliases cannot shadow them.
	// This must happen before any type resolution triggers ensureImport.
	if typeRef != nil {
		typeRef.ReserveNames(collectParamNames(decl.Methods))
	}

	f.AddImport("github.com/AndroidGoLab/binder/binder", "")
	// The stub's OnTransaction method always references context.Context and
	// *parcel.Parcel, so these imports are needed even for method-less interfaces.
	f.AddImport("context", "")
	f.AddImport("github.com/AndroidGoLab/binder/parcel", "")

	interfaceName := AIDLToGoName(decl.IntfName)
	proxyName := deriveProxyName(interfaceName)
	descriptorConst := "Descriptor" + interfaceName

	f.P("// Code generated by aidlgen. DO NOT EDIT.")
	f.P("")

	// Descriptor constant.
	f.P("const %s = %q", descriptorConst, qualifiedName)
	f.P("")

	// Transaction code constants.
	writeTransactionCodes(f, decl.Methods, interfaceName)

	// Interface type.
	writeInterfaceType(f, decl, interfaceName, typeRef)

	// Constants declared inside the interface.
	if len(decl.Constants) > 0 {
		if err := GenerateConstants(decl.Constants, f, interfaceName); err != nil {
			return nil, fmt.Errorf("generating interface constants: %w", err)
		}
		f.P("")
	}

	// Proxy struct.
	writeProxyStruct(f, interfaceName, proxyName)

	// Proxy methods.
	for i, m := range decl.Methods {
		writeProxyMethod(f, interfaceName, proxyName, descriptorConst, m, i, decl.Oneway, opts, typeRef)
	}

	// Stub type (server-side transaction dispatcher).
	stubName := deriveStubName(interfaceName)
	writeStubType(f, interfaceName, stubName, descriptorConst, decl, opts, typeRef)

	// Server-side helper: constructor returning a full interface
	// implementation backed by a StubBinder for binder registration.
	writeStubConstructor(f, interfaceName, stubName, decl, typeRef)

	return f.Bytes()
}

// collectParamNames returns the deduplicated set of Go identifier names
// that will appear as method parameters in the generated interface file.
// These names must be reserved so that import aliases do not shadow them.
func collectParamNames(methods []*parser.MethodDecl) []string {
	seen := make(map[string]bool)
	for _, m := range methods {
		regularParams, _ := classifyParams(m.Params)
		for _, p := range regularParams {
			name := sanitizeGoIdent(p.ParamName)
			seen[name] = true
		}
	}

	names := make([]string, 0, len(seen))
	for name := range seen {
		names = append(names, name)
	}
	return names
}

// deriveProxyName derives a proxy struct name from an interface name.
// IFoo -> FooProxy, Foo -> FooProxy.
func deriveProxyName(interfaceName string) string {
	base := interfaceName
	if strings.HasPrefix(interfaceName, "I") && len(interfaceName) > 1 {
		base = interfaceName[1:]
	}
	return base + "Proxy"
}

// deriveStubName derives a stub struct name from an interface name.
// IFoo -> FooStub, Foo -> FooStub.
func deriveStubName(interfaceName string) string {
	base := interfaceName
	if strings.HasPrefix(interfaceName, "I") && len(interfaceName) > 1 {
		base = interfaceName[1:]
	}
	return base + "Stub"
}

// writeStubType writes the server-side stub struct, interface compliance
// assertion, and OnTransaction dispatcher method. The stub unmarshals
// incoming binder transactions and dispatches them to a typed Go
// interface implementation.
func writeStubType(
	f *GoFile,
	interfaceName string,
	stubName string,
	descriptorConst string,
	decl *parser.InterfaceDecl,
	opts GenOptions,
	typeRef *TypeRefResolver,
) {
	f.AddImport("fmt", "")

	// Stub struct.
	f.P("// %s dispatches incoming binder transactions", stubName)
	f.P("// to a typed %s implementation.", interfaceName)
	f.P("type %s struct {", stubName)
	f.P("\tImpl %s", interfaceName)
	f.P("\tTransport binder.VersionAwareTransport")
	f.P("}")
	f.P("")

	// Interface compliance assertion.
	f.P("var _ binder.TransactionReceiver = (*%s)(nil)", stubName)
	f.P("")

	// Descriptor returns the AIDL interface descriptor.
	// The binder driver calls INTERFACE_TRANSACTION to verify the
	// remote binder's class before delivering transactions.
	f.P("func (s *%s) Descriptor() string {", stubName)
	f.P("\treturn %s", descriptorConst)
	f.P("}")
	f.P("")

	// OnTransaction method. The parcel parameter is named _data (with
	// underscore) to avoid colliding with import aliases such as
	// "data" for android.hardware.radio.data.
	f.P("func (s *%s) OnTransaction(", stubName)
	f.P("\tctx context.Context,")
	f.P("\tcode binder.TransactionCode,")
	f.P("\t_data *parcel.Parcel,")
	f.P(") (*parcel.Parcel, error) {")

	// ReadInterfaceToken validates that the caller is addressing the
	// correct interface. This must happen before dispatching to any
	// method case, and also for interfaces with no methods (marker
	// interfaces) so that malformed transactions are rejected early.
	f.P("\tif _, _err := _data.ReadInterfaceToken(); _err != nil {")
	f.P("\t\treturn nil, _err")
	f.P("\t}")
	f.P("")
	f.P("\tswitch code {")

	// Track transaction codes to skip duplicate cases. Two AIDL methods
	// can resolve to the same transaction code when one overrides another
	// across AIDL versions; only the first occurrence is generated.
	usedCodes := map[int]bool{}
	counter := 0
	for _, m := range decl.Methods {
		code := counter
		if m.TransactionID != 0 {
			code = m.TransactionID - 1
		}
		if usedCodes[code] {
			counter = code + 1
			continue
		}
		usedCodes[code] = true
		counter = code + 1
		writeStubCase(f, interfaceName, descriptorConst, m, decl.Oneway, opts, typeRef)
	}

	f.P("\tdefault:")
	f.P("\t\treturn nil, fmt.Errorf(\"unknown transaction code %%d\", code)")
	f.P("\t}")
	f.P("}")
	f.P("")
}

// writeStubConstructor generates a server-side constructor that creates a
// StubBinder from a user-provided implementation. It generates:
//   - An IXxxServer interface (like IXxx but without AsBinder)
//   - A private wrapper type satisfying IXxx by delegating methods to
//     the server impl and providing AsBinder via an embedded StubBinder
//   - A NewXxxStub(impl) constructor returning *binder.StubBinder
func writeStubConstructor(
	f *GoFile,
	interfaceName string,
	stubName string,
	decl *parser.InterfaceDecl,
	typeRef *TypeRefResolver,
) {
	serverIntfName := interfaceName + "Server"
	wrapperName := unexport(stubName) + "Wrapper"

	// Server interface: same as the full interface minus AsBinder().
	f.P("// %s is the server-side interface that user implementations", serverIntfName)
	f.P("// provide to New%s. It contains only the business methods,", stubName)
	f.P("// without AsBinder (which is provided by the stub itself).")
	f.P("type %s interface {", serverIntfName)
	for _, m := range decl.Methods {
		f.P("\t%s", methodSignature(m, typeRef))
	}
	f.P("}")
	f.P("")

	// Private wrapper: satisfies the full interface by embedding the
	// server impl and holding a *binder.StubBinder.
	f.P("type %s struct {", wrapperName)
	f.P("\timpl %s", serverIntfName)
	f.P("\tstubBinder *binder.StubBinder")
	f.P("}")
	f.P("")

	f.P("func (w *%s) AsBinder() binder.IBinder {", wrapperName)
	f.P("\treturn w.stubBinder")
	f.P("}")
	f.P("")

	// Delegate each method to the impl.
	for _, m := range decl.Methods {
		goName := AIDLToGoName(m.MethodName)
		hasReturn := m.ReturnType != nil && m.ReturnType.Name != "void"
		regular, _ := classifyParams(m.Params)

		// Method signature.
		f.P("func (w *%s) %s(", wrapperName, goName)
		f.P("\tctx context.Context,")
		for _, param := range regular {
			goType := resolveTypeRef(typeRef, param.Type)
			f.P("\t%s %s,", sanitizeGoIdent(param.ParamName), goType)
		}
		if hasReturn {
			goRetType := resolveTypeRef(typeRef, m.ReturnType)
			f.P(") (%s, error) {", goRetType)
		} else {
			f.P(") error {")
		}

		// Build call args.
		var args []string
		args = append(args, "ctx")
		for _, param := range regular {
			args = append(args, sanitizeGoIdent(param.ParamName))
		}

		f.P("\treturn w.impl.%s(%s)", goName, strings.Join(args, ", "))
		f.P("}")
		f.P("")
	}

	// Interface compliance assertion for the wrapper.
	f.P("var _ %s = (*%s)(nil)", interfaceName, wrapperName)
	f.P("")

	// Constructor: NewXxxStub(impl) IXxx.
	f.P("// New%s creates a server-side %s wrapping the given", stubName, interfaceName)
	f.P("// server implementation. The returned value satisfies %s", interfaceName)
	f.P("// and can be passed to proxy methods; its AsBinder() returns a")
	f.P("// *binder.StubBinder that is auto-registered with the binder")
	f.P("// driver on first use.")
	f.P("func New%s(", stubName)
	f.P("\timpl %s,", serverIntfName)
	f.P(") %s {", interfaceName)
	f.P("\twrapper := &%s{impl: impl}", wrapperName)
	f.P("\tstub := &%s{Impl: wrapper}", stubName)
	f.P("\twrapper.stubBinder = binder.NewStubBinder(stub)")
	f.P("\treturn wrapper")
	f.P("}")
	f.P("")
}

// unexport returns the identifier with the first letter lowercased.
func unexport(s string) string {
	if s == "" {
		return s
	}
	return strings.ToLower(s[:1]) + s[1:]
}

// writeStubCase writes a single case in the OnTransaction switch for a method.
func writeStubCase(
	f *GoFile,
	interfaceName string,
	descriptorConst string,
	m *parser.MethodDecl,
	interfaceOneway bool,
	opts GenOptions,
	typeRef *TypeRefResolver,
) {
	goName := AIDLToGoName(m.MethodName)
	constName := "Transaction" + interfaceName + goName
	isOneway := m.Oneway || interfaceOneway
	hasReturn := m.ReturnType != nil && m.ReturnType.Name != "void"

	f.P("\tcase %s:", constName)

	// Read parameters from data parcel. Track whether _err has been
	// declared in this case scope so we can use = vs := correctly.
	regularParams, _ := classifyParams(m.Params)
	paramVarNames := make([]string, 0, len(regularParams))
	errDeclared := false

	for _, param := range m.Params {
		if param.Direction == parser.DirectionOut {
			// Out-only params are not read from data; declare a zero
			// value to pass to the implementation.
			varName := "_arg_" + sanitizeGoIdent(param.ParamName)
			goType := resolveTypeRef(typeRef, param.Type)
			f.P("\t\tvar %s %s", varName, goType)
			paramVarNames = append(paramVarNames, varName)
			continue
		}

		_, isIdentity := identityParamNames[param.ParamName]
		if isIdentity && identityFieldForParam(param) != "" {
			// Identity params are still on the wire but discarded by the stub.
			writeStubDiscardParam(f, param, opts, typeRef)
			continue
		}

		varName := "_arg_" + sanitizeGoIdent(param.ParamName)
		paramVarNames = append(paramVarNames, varName)
		declared := writeStubReadParam(f, param, varName, opts, typeRef, errDeclared)
		errDeclared = errDeclared || declared
	}

	// Build the call expression.
	var callArgs []string
	callArgs = append(callArgs, "ctx")
	paramIdx := 0
	for _, param := range m.Params {
		// Skip identity params that were discarded (not added to
		// paramVarNames). DirectionOut identity params ARE in
		// paramVarNames (handled at the top of the read loop), so
		// only skip when the param was actually discarded.
		if param.Direction != parser.DirectionOut && identityFieldForParam(param) != "" {
			continue
		}
		if paramIdx < len(paramVarNames) {
			callArgs = append(callArgs, paramVarNames[paramIdx])
			paramIdx++
		}
	}

	// When there's a return value, _result is always new, so := works.
	// When there's no return value and _err was already declared by
	// param reads, use = to avoid "no new variables" error.
	switch {
	case hasReturn:
		f.P("\t\t_result, _err := s.Impl.%s(%s)", goName, strings.Join(callArgs, ", "))
	case errDeclared:
		f.P("\t\t_err = s.Impl.%s(%s)", goName, strings.Join(callArgs, ", "))
	default:
		f.P("\t\t_err := s.Impl.%s(%s)", goName, strings.Join(callArgs, ", "))
	}

	if isOneway {
		// Oneway methods have no reply; return the error so the
		// stub dispatch layer can log or handle it locally.
		f.P("\t\treturn nil, _err")
	} else {
		// Write the reply parcel.
		f.P("\t\t_reply := parcel.New()")
		f.P("\t\tif _err != nil {")
		f.P("\t\t\tbinder.WriteStatus(_reply, _err)")
		f.P("\t\t\treturn _reply, nil")
		f.P("\t\t}")
		f.P("\t\tbinder.WriteStatus(_reply, nil)")

		if hasReturn {
			writeStubWriteReturn(f, m.ReturnType, opts, typeRef)
		}

		// Write out/inout params back to the reply parcel so the
		// proxy can read the modified values.
		for _, param := range m.Params {
			if param.Direction == parser.DirectionOut || param.Direction == parser.DirectionInOut {
				outVarName := "_arg_" + sanitizeGoIdent(param.ParamName)
				writeStubOutParam(f, param, outVarName, opts, typeRef)
			}
		}

		f.P("\t\treturn _reply, nil")
	}
}

// writeStubOutParam writes a single out/inout parameter value to the
// reply parcel in a stub OnTransaction case.
func writeStubOutParam(
	f *GoFile,
	param *parser.ParamDecl,
	varName string,
	opts GenOptions,
	typeRef *TypeRefResolver,
) {
	if param.Type.IsArray || param.Type.Name == "List" {
		writeStubWriteOutArray(f, param.Type, varName, opts, typeRef)
		return
	}

	if param.Type.Name == "Map" {
		writeStubWriteOutMap(f, param.Type, varName, opts, typeRef)
		return
	}

	info := marshalForTypeWithCycleCheck(param.Type, opts, typeRef)
	if info.WriteExpr == "" {
		return
	}

	writeExpr := strings.ReplaceAll(info.WriteExpr, "_data", "_reply")

	if strings.Contains(info.WriteExpr, ".MarshalParcel") {
		f.P("\t\t_reply.WriteInt32(1)")
		marshalExpr := fmt.Sprintf(writeExpr, varName)
		f.P("\t\tif _err := %s; _err != nil {", marshalExpr)
		f.P("\t\t\treturn nil, _err")
		f.P("\t\t}")
		return
	}

	if info.IsInterface {
		f.P("\t\tbinder.WriteBinderToParcel(ctx, _reply, %s.AsBinder(), s.Transport)", varName)
		return
	}
	if info.IsIBinder {
		f.P("\t\tbinder.WriteBinderToParcel(ctx, _reply, %s, s.Transport)", varName)
		return
	}

	formatted := fmt.Sprintf(writeExpr, varName)
	f.P("\t\t%s", formatted)
}

// writeStubWriteOutArray writes an array out-param to the reply parcel.
func writeStubWriteOutArray(
	f *GoFile,
	ts *parser.TypeSpecifier,
	varName string,
	opts GenOptions,
	typeRef *TypeRefResolver,
) {
	elemType := elementTypeSpec(ts)

	if elemType.Name == "byte" {
		f.P("\t\t_reply.WriteByteArray(%s)", varName)
		return
	}

	elemInfo := marshalForTypeWithCycleCheck(elemType, opts, typeRef)

	f.P("\t\tif %s == nil {", varName)
	f.P("\t\t\t_reply.WriteInt32(-1)")
	f.P("\t\t} else {")
	f.P("\t\t\t_reply.WriteInt32(int32(len(%s)))", varName)

	if elemInfo.WriteExpr != "" {
		f.P("\t\t\tfor _, _item := range %s {", varName)
		writeExpr := fmt.Sprintf(elemInfo.WriteExpr, "_item")
		writeExpr = strings.ReplaceAll(writeExpr, "_data", "_reply")
		switch {
		case strings.Contains(elemInfo.WriteExpr, ".MarshalParcel"):
			f.P("\t\t\t\t_reply.WriteInt32(1)")
			f.P("\t\t\t\tif _err := %s; _err != nil {", writeExpr)
			f.P("\t\t\t\t\treturn nil, _err")
			f.P("\t\t\t\t}")
		case elemInfo.IsInterface:
			f.P("\t\t\t\tbinder.WriteBinderToParcel(ctx, _reply, _item.AsBinder(), s.Transport)")
		case elemInfo.IsIBinder:
			f.P("\t\t\t\tbinder.WriteBinderToParcel(ctx, _reply, _item, s.Transport)")
		default:
			f.P("\t\t\t\t%s", writeExpr)
		}
		f.P("\t\t\t}")
	}

	f.P("\t\t}")
}

// writeStubWriteOutMap writes a Map out-param to the reply parcel in a
// stub OnTransaction case. Uses stub-appropriate error returns (nil, _err).
func writeStubWriteOutMap(
	f *GoFile,
	ts *parser.TypeSpecifier,
	varName string,
	opts GenOptions,
	typeRef *TypeRefResolver,
) {
	keyType := mapKeyTypeSpec(ts)
	valType := mapValTypeSpec(ts)
	keyInfo := marshalForTypeWithCycleCheck(keyType, opts, typeRef)
	valInfo := marshalForTypeWithCycleCheck(valType, opts, typeRef)

	f.P("\t\tif %s == nil {", varName)
	f.P("\t\t\t_reply.WriteInt32(-1)")
	f.P("\t\t} else {")
	f.P("\t\t\t_reply.WriteInt32(int32(len(%s)))", varName)

	if keyInfo.WriteExpr != "" && valInfo.WriteExpr != "" {
		f.P("\t\t\tfor _k, _v := range %s {", varName)

		keyVar := mapRangeVarWithAssertion("_k", keyType, ts, 0, typeRef)
		valVar := mapRangeVarWithAssertion("_v", valType, ts, 1, typeRef)
		writeKeyExpr := fmt.Sprintf(keyInfo.WriteExpr, keyVar)
		writeKeyExpr = strings.ReplaceAll(writeKeyExpr, "_data", "_reply")
		writeValExpr := fmt.Sprintf(valInfo.WriteExpr, valVar)
		writeValExpr = strings.ReplaceAll(writeValExpr, "_data", "_reply")

		stubErrReturn := "return nil, _err"
		writeMapElemExpr(f, keyInfo, writeKeyExpr, stubErrReturn, "\t\t\t\t", "_reply", "s.Transport", keyVar)
		writeMapElemExpr(f, valInfo, writeValExpr, stubErrReturn, "\t\t\t\t", "_reply", "s.Transport", valVar)

		f.P("\t\t\t}")
	}

	f.P("\t\t}")
}

// writeStubDiscardParam generates code to read (and discard) an identity
// parameter from the data parcel. The wire format includes these fields
// even though the stub does not pass them to the implementation.
func writeStubDiscardParam(
	f *GoFile,
	param *parser.ParamDecl,
	opts GenOptions,
	typeRef *TypeRefResolver,
) {
	info := marshalForTypeWithCycleCheck(param.Type, opts, typeRef)
	readExpr := stubDataReadExpr(info.ReadExpr)
	if readExpr == "" {
		return
	}

	if strings.Contains(info.ReadExpr, ".UnmarshalParcel") {
		// Parcelable identity param (unusual, but handle gracefully).
		return
	}

	f.P("\t\tif _, _err := %s; _err != nil {", readExpr)
	f.P("\t\t\treturn nil, _err")
	f.P("\t\t}")
}

// writeStubReadParam generates code to read a single parameter from the
// data parcel into a local variable. errDeclared indicates whether _err
// has already been declared in the enclosing scope. Returns true if this
// call declares _err (via := with a read expression).
func writeStubReadParam(
	f *GoFile,
	param *parser.ParamDecl,
	varName string,
	opts GenOptions,
	typeRef *TypeRefResolver,
	errDeclared bool,
) bool {
	if param.Type.IsArray || param.Type.Name == "List" {
		writeStubReadArrayParam(f, param.Type, varName, opts, typeRef)
		return false
	}

	if param.Type.Name == "Map" {
		writeStubReadMapParam(f, param.Type, varName, opts, typeRef)
		return false
	}

	info := marshalForTypeWithCycleCheck(param.Type, opts, typeRef)
	readExpr := stubDataReadExpr(info.ReadExpr)
	if readExpr == "" {
		goType := resolveTypeRef(typeRef, param.Type)
		f.P("\t\tvar %s %s", varName, goType)
		return false
	}

	isNullable := hasAnnotation(param.Type.Annots, "nullable")

	if strings.Contains(info.ReadExpr, ".UnmarshalParcel") {
		goType := resolveTypeRef(typeRef, param.Type)
		// Parcelable: read null indicator, then unmarshal.
		// Use a block scope to avoid _nullInd redeclaration across
		// multiple parcelable params.
		f.P("\t\tvar %s %s", varName, goType)
		f.P("\t\t{")
		f.P("\t\t\t_nullInd, _err := _data.ReadInt32()")
		f.P("\t\t\tif _err != nil {")
		f.P("\t\t\t\treturn nil, _err")
		f.P("\t\t\t}")
		f.P("\t\t\tif _nullInd != 0 {")
		if isNullable && len(goType) > 0 && goType[0] == '*' {
			// @nullable parcelable: allocate the struct before
			// unmarshaling to avoid nil pointer dereference. When
			// _nullInd == 0 the variable stays nil, which is correct.
			baseType := goType[1:]
			f.P("\t\t\t\t%s = new(%s)", varName, baseType)
		}
		f.P("\t\t\t\tif _err = %s.UnmarshalParcel(_data); _err != nil {", varName)
		f.P("\t\t\t\t\treturn nil, _err")
		f.P("\t\t\t\t}")
		f.P("\t\t\t}")
		f.P("\t\t}")
		// _err was declared inside a block scope, so it does not affect
		// the outer scope.
		return false
	}

	if info.IsInterface {
		// Read the binder handle and wrap it in a typed proxy.
		// Stubs don't have a transport or identity, so pass nil/zero.
		goType := resolveTypeRef(typeRef, param.Type)
		// Go interfaces are inherently nullable (nil-able), so strip
		// the pointer from @nullable interface types like *IFoo.
		goType = strings.TrimPrefix(goType, "*")
		proxyConstructor := interfaceProxyConstructor(typeRef, param.Type)
		f.P("\t\tvar %s %s", varName, goType)
		f.P("\t\t{")
		f.P("\t\t\t_%sHandle, _err := _data.ReadStrongBinder()", param.ParamName)
		f.P("\t\t\tif _err != nil {")
		f.P("\t\t\t\treturn nil, _err")
		f.P("\t\t\t}")
		f.P("\t\t\t%s = %s(binder.NewProxyBinder(s.Transport, binder.CallerIdentity{}, _%sHandle))", varName, proxyConstructor, param.ParamName)
		f.P("\t\t}")
		// _err was declared inside a block scope, so it does not affect
		// the outer scope.
		return false
	}

	if info.IsIBinder {
		// Read a raw binder handle and wrap it in a ProxyBinder.
		// Stubs don't have a transport or identity, so pass nil/zero.
		goType := resolveTypeRef(typeRef, param.Type)
		f.P("\t\tvar %s %s", varName, goType)
		f.P("\t\t{")
		f.P("\t\t\t_%sHandle, _err := _data.ReadStrongBinder()", param.ParamName)
		f.P("\t\t\tif _err != nil {")
		f.P("\t\t\t\treturn nil, _err")
		f.P("\t\t\t}")
		f.P("\t\t\t%s = binder.NewProxyBinder(s.Transport, binder.CallerIdentity{}, _%sHandle)", varName, param.ParamName)
		f.P("\t\t}")
		// _err was declared inside a block scope, so it does not affect
		// the outer scope.
		return false
	}

	if info.NeedsCast {
		goType := resolveTypeRef(typeRef, param.Type)
		rawVar := "_raw_" + sanitizeGoIdent(param.ParamName)
		// rawVar is always new, so := works regardless of errDeclared.
		f.P("\t\t%s, _err := %s", rawVar, readExpr)
		f.P("\t\tif _err != nil {")
		f.P("\t\t\treturn nil, _err")
		f.P("\t\t}")
		if isNullable && len(goType) > 0 && goType[0] == '*' {
			baseType := goType[1:]
			f.P("\t\t_%s := %s(%s)", sanitizeGoIdent(param.ParamName), baseType, rawVar)
			f.P("\t\t%s := &_%s", varName, sanitizeGoIdent(param.ParamName))
		} else {
			f.P("\t\t%s := %s(%s)", varName, goType, rawVar)
		}
		return true
	}

	goType := resolveTypeRef(typeRef, param.Type)
	if isNullable && len(goType) > 0 && goType[0] == '*' {
		// Nullable non-cast type: read value, then take its address
		// to match the pointer-typed interface parameter.
		rawVar := "_raw_" + sanitizeGoIdent(param.ParamName)
		f.P("\t\t%s, _err := %s", rawVar, readExpr)
		f.P("\t\tif _err != nil {")
		f.P("\t\t\treturn nil, _err")
		f.P("\t\t}")
		f.P("\t\t%s := &%s", varName, rawVar)
		return true
	}

	f.P("\t\t%s, _err := %s", varName, readExpr)
	f.P("\t\tif _err != nil {")
	f.P("\t\t\treturn nil, _err")
	f.P("\t\t}")
	// varName is always new, so := always works.
	return true
}

// writeStubReadArrayParam generates code to read an array or List parameter
// from the data parcel into a local variable inside a stub OnTransaction case.
// All intermediate errors are declared in a block scope so they do not
// affect the outer _err variable.
func writeStubReadArrayParam(
	f *GoFile,
	ts *parser.TypeSpecifier,
	varName string,
	opts GenOptions,
	typeRef *TypeRefResolver,
) {
	goType := resolveTypeRef(typeRef, ts)
	elemType := elementTypeSpec(ts)

	// Compact byte[] uses ReadByteArray instead of per-element reads.
	if elemType.Name == "byte" {
		f.P("\t\tvar %s %s", varName, goType)
		f.P("\t\t{")
		f.P("\t\t\t_bytes, _err := _data.ReadByteArray()")
		f.P("\t\t\tif _err != nil {")
		f.P("\t\t\t\treturn nil, _err")
		f.P("\t\t\t}")
		f.P("\t\t\t%s = _bytes", varName)
		f.P("\t\t}")
		return
	}

	elemInfo := marshalForTypeWithCycleCheck(elemType, opts, typeRef)
	elemReadExpr := stubDataReadExpr(elemInfo.ReadExpr)

	f.P("\t\tvar %s %s", varName, goType)
	f.P("\t\t{")
	f.P("\t\t\t_count, _err := _data.ReadInt32()")
	f.P("\t\t\tif _err != nil {")
	f.P("\t\t\t\treturn nil, _err")
	f.P("\t\t\t}")
	f.P("\t\t\tif _count > 1000000 {")
	f.P("\t\t\t\treturn nil, fmt.Errorf(\"array count too large: %%d\", _count)")
	f.P("\t\t\t}")
	f.P("\t\t\tif _count >= 0 {")
	f.P("\t\t\t\t%s = make(%s, _count)", varName, goType)

	if elemReadExpr != "" {
		f.P("\t\t\t\tfor _i := int32(0); _i < _count; _i++ {")

		switch {
		case strings.Contains(elemInfo.ReadExpr, ".UnmarshalParcel"):
			// Parcelable element: read null indicator, then unmarshal.
			f.P("\t\t\t\t\tif _, _err = _data.ReadInt32(); _err != nil {")
			f.P("\t\t\t\t\t\treturn nil, _err")
			f.P("\t\t\t\t\t}")
			f.P("\t\t\t\t\tif _err = %s[_i].UnmarshalParcel(_data); _err != nil {", varName)
			f.P("\t\t\t\t\t\treturn nil, _err")
			f.P("\t\t\t\t\t}")
		case elemInfo.IsInterface:
			proxyConstructor := interfaceProxyConstructor(typeRef, elemType)
			f.P("\t\t\t\t\t_handle, _err := %s", elemReadExpr)
			f.P("\t\t\t\t\tif _err != nil {")
			f.P("\t\t\t\t\t\treturn nil, _err")
			f.P("\t\t\t\t\t}")
			f.P("\t\t\t\t\t%s[_i] = %s(binder.NewProxyBinder(s.Transport, binder.CallerIdentity{}, _handle))", varName, proxyConstructor)
		case elemInfo.IsIBinder:
			f.P("\t\t\t\t\t_handle, _err := %s", elemReadExpr)
			f.P("\t\t\t\t\tif _err != nil {")
			f.P("\t\t\t\t\t\treturn nil, _err")
			f.P("\t\t\t\t\t}")
			f.P("\t\t\t\t\t%s[_i] = binder.NewProxyBinder(s.Transport, binder.CallerIdentity{}, _handle)", varName)
		case elemInfo.NeedsCast:
			elemGoType := resolveTypeRef(typeRef, elemType)
			f.P("\t\t\t\t\t_raw, _err := %s", elemReadExpr)
			f.P("\t\t\t\t\tif _err != nil {")
			f.P("\t\t\t\t\t\treturn nil, _err")
			f.P("\t\t\t\t\t}")
			f.P("\t\t\t\t\t%s[_i] = %s(_raw)", varName, elemGoType)
		default:
			f.P("\t\t\t\t\t%s[_i], _err = %s", varName, elemReadExpr)
			f.P("\t\t\t\t\tif _err != nil {")
			f.P("\t\t\t\t\t\treturn nil, _err")
			f.P("\t\t\t\t\t}")
		}

		f.P("\t\t\t\t}")
	}

	f.P("\t\t\t}")
	f.P("\t\t}")
}

// writeStubReadMapParam generates code to read a Map parameter from the
// data parcel into a local variable inside a stub OnTransaction case.
func writeStubReadMapParam(
	f *GoFile,
	ts *parser.TypeSpecifier,
	varName string,
	opts GenOptions,
	typeRef *TypeRefResolver,
) {
	goType := resolveTypeRef(typeRef, ts)
	keyType := mapKeyTypeSpec(ts)
	valType := mapValTypeSpec(ts)
	keyInfo := marshalForTypeWithCycleCheck(keyType, opts, typeRef)
	valInfo := marshalForTypeWithCycleCheck(valType, opts, typeRef)

	keyReadExpr := stubDataReadExpr(keyInfo.ReadExpr)
	valReadExpr := stubDataReadExpr(valInfo.ReadExpr)

	f.P("\t\tvar %s %s", varName, goType)
	f.P("\t\t{")
	f.P("\t\t\t_mapCount, _err := _data.ReadInt32()")
	f.P("\t\t\tif _err != nil {")
	f.P("\t\t\t\treturn nil, _err")
	f.P("\t\t\t}")
	f.P("\t\t\tif _mapCount >= 0 {")
	f.P("\t\t\t\t%s = make(%s, _mapCount)", varName, goType)

	valIsList := valType.IsArray || valType.Name == "List"
	keyIsList := keyType.IsArray || keyType.Name == "List"

	if (keyReadExpr != "" || keyIsList) && (valReadExpr != "" || valIsList) {
		f.P("\t\t\t\tfor _mi := int32(0); _mi < _mapCount; _mi++ {")

		if keyIsList {
			writeMapReadArrayElem(f, keyType, "_mk", "\t\t\t\t\t", opts, typeRef, "_data", "return nil, _err")
		} else {
			writeStubMapReadElem(f, keyInfo, keyReadExpr, keyType, "_mk", "\t\t\t\t\t", typeRef, "k")
		}

		if valIsList {
			writeMapReadArrayElem(f, valType, "_mv", "\t\t\t\t\t", opts, typeRef, "_data", "return nil, _err")
		} else {
			writeStubMapReadElem(f, valInfo, valReadExpr, valType, "_mv", "\t\t\t\t\t", typeRef, "v")
		}

		f.P("\t\t\t\t\t%s[_mk] = _mv", varName)
		f.P("\t\t\t\t}")
	}

	f.P("\t\t\t}")
	f.P("\t\t}")
}

// writeStubMapReadElem writes the code to read a single map element (key or
// value) into a local variable within a stub's OnTransaction handler.
// Unlike the proxy-side writeMapReadElem, error returns use (nil, _err)
// since stubs return (*parcel.Parcel, error).
// rawPrefix disambiguates the intermediate _raw variable when both key and
// value use NeedsCast (e.g., "k" produces _rawK, "v" produces _rawV).
func writeStubMapReadElem(
	f *GoFile,
	info MarshalInfo,
	readExpr string,
	ts *parser.TypeSpecifier,
	varName string,
	indent string,
	typeRef *TypeRefResolver,
	rawPrefix string,
) {
	switch {
	case strings.Contains(info.ReadExpr, ".UnmarshalParcel"):
		goType := resolveTypeRef(typeRef, ts)
		f.P("%svar %s %s", indent, varName, goType)
		f.P("%sif _, _err = _data.ReadInt32(); _err != nil {", indent)
		f.P("%s\treturn nil, _err", indent)
		f.P("%s}", indent)
		f.P("%sif _err = %s.UnmarshalParcel(_data); _err != nil {", indent, varName)
		f.P("%s\treturn nil, _err", indent)
		f.P("%s}", indent)
	case info.NeedsCast:
		goType := resolveTypeRef(typeRef, ts)
		rawVar := fmt.Sprintf("_raw%s", strings.ToUpper(rawPrefix[:1])+rawPrefix[1:])
		f.P("%s%s, _err := %s", indent, rawVar, readExpr)
		f.P("%sif _err != nil {", indent)
		f.P("%s\treturn nil, _err", indent)
		f.P("%s}", indent)
		f.P("%s%s := %s(%s)", indent, varName, goType, rawVar)
	default:
		f.P("%s%s, _err := %s", indent, varName, readExpr)
		f.P("%sif _err != nil {", indent)
		f.P("%s\treturn nil, _err", indent)
		f.P("%s}", indent)
	}
}

// writeStubWriteReturn generates code to write a return value to the
// reply parcel.
func writeStubWriteReturn(
	f *GoFile,
	retType *parser.TypeSpecifier,
	opts GenOptions,
	typeRef *TypeRefResolver,
) {
	if retType.IsArray || retType.Name == "List" {
		elemType := elementTypeSpec(retType)

		// AIDL uses compact writeByteArray for byte[], not per-element.
		if elemType.Name == "byte" {
			f.P("\t\t_reply.WriteByteArray(_result)")
			return
		}

		elemInfo := marshalForTypeWithCycleCheck(elemType, opts, typeRef)

		f.P("\t\tif _result == nil {")
		f.P("\t\t\t_reply.WriteInt32(-1)")
		f.P("\t\t} else {")
		f.P("\t\t\t_reply.WriteInt32(int32(len(_result)))")

		if elemInfo.WriteExpr != "" {
			f.P("\t\t\tfor _, _item := range _result {")
			writeExpr := fmt.Sprintf(elemInfo.WriteExpr, "_item")
			writeExpr = strings.ReplaceAll(writeExpr, "_data", "_reply")
			switch {
			case strings.Contains(elemInfo.WriteExpr, ".MarshalParcel"):
				f.P("\t\t\t\t_reply.WriteInt32(1)")
				f.P("\t\t\t\tif _err := %s; _err != nil {", writeExpr)
				f.P("\t\t\t\t\treturn nil, _err")
				f.P("\t\t\t\t}")
			case elemInfo.IsInterface:
				f.P("\t\t\t\tbinder.WriteBinderToParcel(ctx, _reply, _item.AsBinder(), s.Transport)")
			case elemInfo.IsIBinder:
				f.P("\t\t\t\tbinder.WriteBinderToParcel(ctx, _reply, _item, s.Transport)")
			default:
				f.P("\t\t\t\t%s", writeExpr)
			}
			f.P("\t\t\t}")
		}

		f.P("\t\t}")
		return
	}

	if retType.Name == "Map" {
		keyType := mapKeyTypeSpec(retType)
		valType := mapValTypeSpec(retType)
		keyInfo := marshalForTypeWithCycleCheck(keyType, opts, typeRef)
		valInfo := marshalForTypeWithCycleCheck(valType, opts, typeRef)

		f.P("\t\tif _result == nil {")
		f.P("\t\t\t_reply.WriteInt32(-1)")
		f.P("\t\t} else {")
		f.P("\t\t\t_reply.WriteInt32(int32(len(_result)))")

		valIsList := valType.IsArray || valType.Name == "List"
		keyIsList := keyType.IsArray || keyType.Name == "List"

		if keyInfo.WriteExpr != "" || keyIsList || valInfo.WriteExpr != "" || valIsList {
			f.P("\t\t\tfor _k, _v := range _result {")

			keyVar := mapRangeVarWithAssertion("_k", keyType, retType, 0, typeRef)
			valVar := mapRangeVarWithAssertion("_v", valType, retType, 1, typeRef)
			stubErrReturn := "return nil, _err"

			if keyIsList {
				writeMapWriteArrayElem(f, keyType, keyVar, "\t\t\t\t", opts, typeRef, "_reply", stubErrReturn)
			} else {
				writeKeyExpr := fmt.Sprintf(keyInfo.WriteExpr, keyVar)
				writeKeyExpr = strings.ReplaceAll(writeKeyExpr, "_data", "_reply")
				writeMapElemExpr(f, keyInfo, writeKeyExpr, stubErrReturn, "\t\t\t\t", "_reply", "s.Transport", keyVar)
			}

			if valIsList {
				writeMapWriteArrayElem(f, valType, valVar, "\t\t\t\t", opts, typeRef, "_reply", stubErrReturn)
			} else {
				writeValExpr := fmt.Sprintf(valInfo.WriteExpr, valVar)
				writeValExpr = strings.ReplaceAll(writeValExpr, "_data", "_reply")
				writeMapElemExpr(f, valInfo, writeValExpr, stubErrReturn, "\t\t\t\t", "_reply", "s.Transport", valVar)
			}

			f.P("\t\t\t}")
		}

		f.P("\t\t}")
		return
	}

	info := marshalForTypeWithCycleCheck(retType, opts, typeRef)
	writeExpr := stubReplyWriteExpr(info.WriteExpr)
	if writeExpr == "" {
		f.P("\t\t_ = _result")
		return
	}

	if strings.Contains(info.WriteExpr, ".MarshalParcel") {
		// Parcelable return: write non-null indicator + marshal.
		f.P("\t\t_reply.WriteInt32(1)")
		marshalExpr := strings.ReplaceAll(info.WriteExpr, "_data", "_reply")
		marshalExpr = fmt.Sprintf(marshalExpr, "_result")
		f.P("\t\tif _err := %s; _err != nil {", marshalExpr)
		f.P("\t\t\treturn nil, _err")
		f.P("\t\t}")
		return
	}

	if info.IsInterface {
		f.P("\t\tbinder.WriteBinderToParcel(ctx, _reply, _result.AsBinder(), s.Transport)")
		return
	}
	if info.IsIBinder {
		f.P("\t\tbinder.WriteBinderToParcel(ctx, _reply, _result, s.Transport)")
		return
	}

	formatted := fmt.Sprintf(writeExpr, "_result")
	f.P("\t\t%s", formatted)
}

// stubDataReadExpr converts a MarshalInfo ReadExpr (which references _reply)
// to read from the _data parcel parameter instead.
func stubDataReadExpr(readExpr string) string {
	if readExpr == "" {
		return ""
	}
	return strings.ReplaceAll(readExpr, "_reply", "_data")
}

// stubReplyWriteExpr converts a MarshalInfo WriteExpr (which references _data)
// to write to the _reply parcel instead.
func stubReplyWriteExpr(writeExpr string) string {
	if writeExpr == "" {
		return ""
	}
	return strings.ReplaceAll(writeExpr, "_data", "_reply")
}

// writeTransactionCodes writes const declarations for each method's transaction code.
func writeTransactionCodes(
	f *GoFile,
	methods []*parser.MethodDecl,
	interfaceName string,
) {
	if len(methods) == 0 {
		return
	}

	codes := ComputeTransactionCodes(methods)

	f.P("const (")
	for _, m := range methods {
		constName := "Transaction" + interfaceName + AIDLToGoName(m.MethodName)
		code := codes[m.MethodName]
		f.P("\t%s = binder.FirstCallTransaction + %d", constName, int(code-binder.FirstCallTransaction))
	}
	f.P(")")
	f.P("")

	f.P("const (")
	for _, m := range methods {
		constName := "Method" + interfaceName + AIDLToGoName(m.MethodName)
		f.P("\t%s = %q", constName, m.MethodName)
	}
	f.P(")")
	f.P("")
}

// writeInterfaceType writes the Go interface type declaration.
func writeInterfaceType(
	f *GoFile,
	decl *parser.InterfaceDecl,
	interfaceName string,
	typeRef *TypeRefResolver,
) {
	f.P("type %s interface {", interfaceName)
	f.P("\tAsBinder() binder.IBinder")
	for _, m := range decl.Methods {
		f.P("\t%s", methodSignature(m, typeRef))
	}
	f.P("}")
	f.P("")
}

// writeProxyStruct writes the proxy struct, constructor, and interface compliance check.
func writeProxyStruct(
	f *GoFile,
	interfaceName string,
	proxyName string,
) {
	f.P("type %s struct {", proxyName)
	f.P("\tRemote binder.IBinder")
	f.P("}")
	f.P("")

	f.P("func New%s(", proxyName)
	f.P("\tremote binder.IBinder,")
	f.P(") *%s {", proxyName)
	f.P("\treturn &%s{Remote: remote}", proxyName)
	f.P("}")
	f.P("")

	f.P("func (p *%s) AsBinder() binder.IBinder {", proxyName)
	f.P("\treturn p.Remote")
	f.P("}")
	f.P("")

	f.P("var _ %s = (*%s)(nil)", interfaceName, proxyName)
	f.P("")
}

// writeProxyMethod writes a single proxy method implementation.
func writeProxyMethod(
	f *GoFile,
	interfaceName string,
	proxyName string,
	descriptorConst string,
	m *parser.MethodDecl,
	index int,
	interfaceOneway bool,
	opts GenOptions,
	typeRef *TypeRefResolver,
) {
	goName := AIDLToGoName(m.MethodName)
	isOneway := m.Oneway || interfaceOneway
	hasReturn := m.ReturnType != nil && m.ReturnType.Name != "void"

	// Classify parameters: some are auto-filled from CallerIdentity
	// and omitted from the proxy method signature.
	regularParams, identityParams := classifyParams(m.Params)
	hasIdentity := len(identityParams) > 0

	// Method signature — identity params are omitted.
	f.P("func (p *%s) %s(", proxyName, goName)
	f.P("\tctx context.Context,")
	for _, param := range regularParams {
		goType := resolveTypeRef(typeRef, param.Type)
		f.P("\t%s %s,", sanitizeGoIdent(param.ParamName), goType)
	}
	if hasReturn {
		goRetType := resolveTypeRef(typeRef, m.ReturnType)
		f.P(") (%s, error) {", goRetType)
	} else {
		f.P(") error {")
	}

	// Declare _result early so it is available in error paths.
	if hasReturn {
		f.P("\tvar _result %s", resolveTypeRef(typeRef, m.ReturnType))
	}

	// Retrieve caller identity when any parameters are auto-filled.
	if hasIdentity {
		f.P("\t_identity := p.Remote.Identity()")
	}

	// Build and write data parcel.
	f.P("\t_data := parcel.New()")
	f.P("\tdefer _data.Recycle()")
	f.P("\t_data.WriteInterfaceToken(%s)", descriptorConst)

	for i, param := range m.Params {
		if param.Direction == parser.DirectionOut {
			continue
		}

		// Version-dependent params are wrapped in a conditional that
		// checks the device API level. The == 0 fallback ensures the
		// param is written when API level is unknown (backward compat).
		if param.MinAPILevel > 0 {
			f.P("if binder.APILevelFromBinder(p.Remote) == 0 || binder.APILevelFromBinder(p.Remote) >= %d {", param.MinAPILevel)
		}

		// Identity params are written from _identity instead of a
		// method parameter.
		if field, ok := identityParams[i]; ok {
			expr := fmt.Sprintf(identityWriteExpr[param.Type.Name], "_identity."+field)
			f.P("\t%s", expr)
			if param.MinAPILevel > 0 {
				f.P("}")
			}
			continue
		}

		writeParamToParcel(f, param, hasReturn, opts, typeRef)
		if param.MinAPILevel > 0 {
			f.P("}")
		}
	}

	// Determine flags.
	flags := "0"
	if isOneway {
		flags = "binder.FlagOneway"
	}

	// Transact call and reply handling.
	// Use ResolveCode so the proxy asks the transport for the correct
	// transaction code. The proxy returns an error if the method cannot
	// be resolved, instead of silently falling back to a compiled constant.
	methodConst := "Method" + interfaceName + AIDLToGoName(m.MethodName)
	f.AddImport("fmt", "")
	f.P("")
	f.P("\t_code, _err := p.Remote.ResolveCode(ctx, %s, %s)", descriptorConst, methodConst)
	f.P("\tif _err != nil {")
	if hasReturn {
		f.P("\t\treturn _result, fmt.Errorf(\"resolving %%s.%%s: %%w\", %s, %s, _err)", descriptorConst, methodConst)
	} else {
		f.P("\t\treturn fmt.Errorf(\"resolving %%s.%%s: %%w\", %s, %s, _err)", descriptorConst, methodConst)
	}
	f.P("\t}")
	f.P("")
	if isOneway {
		// Oneway methods do not receive a reply from the kernel.
		f.P("\t_, _err = p.Remote.Transact(ctx, _code, %s, _data)", flags)
		if hasReturn {
			f.P("\treturn _result, _err")
		} else {
			f.P("\treturn _err")
		}
	} else {
		f.P("\t_reply, _err := p.Remote.Transact(ctx, _code, %s, _data)", flags)
		if hasReturn {
			f.P("\tif _err != nil {")
			f.P("\t\treturn _result, _err")
			f.P("\t}")
		} else {
			f.P("\tif _err != nil {")
			f.P("\t\treturn _err")
			f.P("\t}")
		}
		f.P("\tdefer _reply.Recycle()")

		// Read status.
		f.P("")
		f.P("\tif _err = binder.ReadStatus(_reply); _err != nil {")
		if hasReturn {
			f.P("\t\treturn _result, _err")
		} else {
			f.P("\t\treturn _err")
		}
		f.P("\t}")

		// Read out/inout parameters.
		outArrayCounter := 0
		for _, param := range m.Params {
			if param.Direction == parser.DirectionOut || param.Direction == parser.DirectionInOut {
				readOutParam(f, param, hasReturn, opts, typeRef, &outArrayCounter)
			}
		}

		// Read return value.
		if hasReturn {
			f.P("")
			readReturnValue(f, m.ReturnType, opts, typeRef)
			f.P("\treturn _result, nil")
		} else {
			f.P("")
			f.P("\treturn nil")
		}
	}

	f.P("}")
	f.P("")
}

// writeParamToParcel writes the parcel serialization for a single input parameter.
// hasReturn indicates whether the enclosing method has a return value, which
// affects the shape of early-return error statements.
func writeParamToParcel(
	f *GoFile,
	param *parser.ParamDecl,
	hasReturn bool,
	opts GenOptions,
	typeRef *TypeRefResolver,
) {
	varName := sanitizeGoIdent(param.ParamName)

	if param.Type.IsArray || param.Type.Name == "List" {
		writeArrayToParcel(f, param.Type, varName, hasReturn, opts, typeRef)
		return
	}

	if param.Type.Name == "Map" {
		writeMapToParcel(f, param.Type, varName, hasReturn, opts, typeRef, "_data", "\t", "p.Remote.Transport()")
		return
	}

	info := marshalForTypeWithCycleCheck(param.Type, opts, typeRef)
	if info.WriteExpr == "" {
		goType := resolveTypeRef(typeRef, param.Type)
		f.P("\t// WARNING: param %s (type %s) cannot be serialized — type not resolved", varName, goType)
		return
	}

	isNullable := hasAnnotation(param.Type.Annots, "nullable")

	// For nullable parameters, the Go type is a pointer. We need to check for
	// nil and dereference the pointer before passing to the write expression.
	if isNullable {
		goType := resolveTypeRef(typeRef, param.Type)
		if len(goType) > 0 && goType[0] == '*' {
			f.P("\tif %s != nil {", varName)
			writeExpr := fmt.Sprintf(info.WriteExpr, "(*"+varName+")")
			if strings.Contains(info.WriteExpr, ".MarshalParcel") {
				// AIDL wire format: int32(1) as non-null indicator
				// before the parcelable data, matching writeTypedObject
				// semantics. The null branch writes int32(-1).
				f.P("\t\t_data.WriteInt32(1)")
				f.P("\t\tif _err := %s; _err != nil {", writeExpr)
				if hasReturn {
					f.P("\t\t\treturn _result, _err")
				} else {
					f.P("\t\t\treturn _err")
				}
				f.P("\t\t}")
			} else {
				f.P("\t\t%s", writeExpr)
			}
			f.P("\t} else {")
			f.P("\t\t_data.WriteInt32(-1)")
			f.P("\t}")
			return
		}
	}

	// Interface and IBinder parameters use WriteBinderToParcel to support
	// both remote proxies (BINDER_TYPE_HANDLE) and local stubs
	// (BINDER_TYPE_BINDER) transparently.
	if info.IsInterface {
		f.P("\tbinder.WriteBinderToParcel(ctx, _data, %s.AsBinder(), p.Remote.Transport())", varName)
		return
	}
	if info.IsIBinder {
		f.P("\tbinder.WriteBinderToParcel(ctx, _data, %s, p.Remote.Transport())", varName)
		return
	}

	writeExpr := fmt.Sprintf(info.WriteExpr, varName)

	if strings.Contains(info.WriteExpr, ".MarshalParcel") {
		// Java's writeTypedObject writes int32(1) as a non-null indicator
		// before calling writeToParcel. The server's readTypedObject reads
		// this indicator before calling createFromParcel.
		f.P("\t_data.WriteInt32(1)")
		f.P("\tif _err := %s; _err != nil {", writeExpr)
		if hasReturn {
			f.P("\t\treturn _result, _err")
		} else {
			f.P("\t\treturn _err")
		}
		f.P("\t}")
		return
	}

	f.P("\t%s", writeExpr)
}

// writeArrayToParcel writes array/list serialization code.
// hasReturn indicates whether the enclosing method has a return value, which
// affects the shape of early-return error statements.
func writeArrayToParcel(
	f *GoFile,
	ts *parser.TypeSpecifier,
	varName string,
	hasReturn bool,
	opts GenOptions,
	typeRef *TypeRefResolver,
) {
	elemType := elementTypeSpec(ts)

	// AIDL uses compact writeByteArray for byte[], not per-element
	// WritePaddedByte. This matches the NDK AParcel_writeByteArray format.
	if elemType.Name == "byte" {
		f.P("\t_data.WriteByteArray(%s)", varName)
		return
	}

	elemInfo := marshalForTypeWithCycleCheck(elemType, opts, typeRef)

	f.P("\tif %s == nil {", varName)
	f.P("\t\t_data.WriteInt32(-1)")
	f.P("\t} else {")
	f.P("\t\t_data.WriteInt32(int32(len(%s)))", varName)

	if elemInfo.WriteExpr != "" {
		f.P("\t\tfor _, _item := range %s {", varName)
		writeExpr := fmt.Sprintf(elemInfo.WriteExpr, "_item")
		switch {
		case strings.Contains(elemInfo.WriteExpr, ".MarshalParcel"):
			// NDK AIDL wire format: int32(1) non-null indicator before each
			// parcelable element, matching writeTypedObject semantics.
			f.P("\t\t\t_data.WriteInt32(1)")
			f.P("\t\t\tif _err := %s; _err != nil {", writeExpr)
			if hasReturn {
				f.P("\t\t\t\treturn _result, _err")
			} else {
				f.P("\t\t\t\treturn _err")
			}
			f.P("\t\t\t}")
		case elemInfo.IsInterface:
			// Interface elements must use WriteBinderToParcel to handle
			// both remote proxies and local stubs transparently.
			f.P("\t\t\tbinder.WriteBinderToParcel(ctx, _data, _item.AsBinder(), p.Remote.Transport())")
		case elemInfo.IsIBinder:
			// IBinder elements must use WriteBinderToParcel to handle
			// both remote proxies and local stubs transparently.
			f.P("\t\t\tbinder.WriteBinderToParcel(ctx, _data, _item, p.Remote.Transport())")
		default:
			f.P("\t\t\t%s", writeExpr)
		}
		f.P("\t\t}")
	}

	f.P("\t}")
}

// elementTypeSpec extracts the element type from an array or List type specifier.
// For arrays (T[]), @nullable on the outer type means the array is nullable,
// not each element, so we strip @nullable from the element annotations to
// avoid generating incorrect pointer-type casts like *EnumType(raw).
func elementTypeSpec(ts *parser.TypeSpecifier) *parser.TypeSpecifier {
	if ts.Name == "List" && len(ts.TypeArgs) > 0 {
		return ts.TypeArgs[0]
	}

	// For T[], return a type spec for T without the array flag.
	// Strip @nullable because it applies to the array, not the elements.
	return &parser.TypeSpecifier{
		Name:     ts.Name,
		Annots:   stripNullableAnnotation(ts.Annots),
		TypeArgs: ts.TypeArgs,
		IsArray:  false,
	}
}

// stripNullableAnnotation returns a copy of the annotation list with @nullable
// removed. Returns nil if the result would be empty.
func stripNullableAnnotation(annots []*parser.Annotation) []*parser.Annotation {
	var filtered []*parser.Annotation
	for _, a := range annots {
		if a.Name == "nullable" {
			continue
		}
		filtered = append(filtered, a)
	}
	return filtered
}

// readReturnValue writes the code to read a return value from a reply parcel.
func readReturnValue(
	f *GoFile,
	retType *parser.TypeSpecifier,
	opts GenOptions,
	typeRef *TypeRefResolver,
) {
	if retType.IsArray || retType.Name == "List" {
		readArrayFromReply(f, retType, opts, typeRef)
		return
	}

	if retType.Name == "Map" {
		readMapFromReply(f, retType, opts, typeRef, "_reply", "_result", "\t", "return _result, _err")
		return
	}

	info := marshalForTypeWithCycleCheck(retType, opts, typeRef)
	if info.ReadExpr == "" {
		// Opaque/cycle-broken type: skip the parcelable data on the wire
		// so the parcel position stays consistent for any subsequent reads.
		// Java-originated parcelables (like ActivityManager.RunningAppProcessInfo)
		// resolve to any but still use the typed-object wire format:
		// int32(null indicator) + int32(size) + payload.
		if isLikelyParcelable(retType) {
			f.AddImport("github.com/AndroidGoLab/binder/parcel", "")
			f.P("\t_nullInd, _err := _reply.ReadInt32()")
			f.P("\tif _err != nil {")
			f.P("\t\treturn _result, _err")
			f.P("\t}")
			f.P("\tif _nullInd != 0 {")
			f.P("\t\t_endPos, _err := parcel.ReadParcelableHeader(_reply)")
			f.P("\t\tif _err != nil {")
			f.P("\t\t\treturn _result, _err")
			f.P("\t\t}")
			f.P("\t\tparcel.SkipToParcelableEnd(_reply, _endPos)")
			f.P("\t}")
		}
		return
	}

	readExpr := info.ReadExpr

	if strings.Contains(info.ReadExpr, ".UnmarshalParcel") {
		// Java's readTypedObject reads int32 null indicator before
		// calling createFromParcel. Skip unmarshaling for null (0).
		f.P("\t_nullIndicator, _err := _reply.ReadInt32()")
		f.P("\tif _err != nil {")
		f.P("\t\treturn _result, _err")
		f.P("\t}")
		f.P("\tif _nullIndicator != 0 {")
		f.P("\t\tif _err = _result.UnmarshalParcel(_reply); _err != nil {")
		f.P("\t\t\treturn _result, _err")
		f.P("\t\t}")
		f.P("\t}")
		return
	}

	if info.IsInterface {
		proxyConstructor := interfaceProxyConstructor(typeRef, retType)
		f.P("\t_handle, _err := %s", readExpr)
		f.P("\tif _err != nil {")
		f.P("\t\treturn _result, _err")
		f.P("\t}")
		f.P("\t_result = %s(binder.NewProxyBinder(p.Remote.Transport(), p.Remote.Identity(), _handle))", proxyConstructor)
		return
	}

	if info.IsIBinder {
		f.P("\t_handle, _err := %s", readExpr)
		f.P("\tif _err != nil {")
		f.P("\t\treturn _result, _err")
		f.P("\t}")
		f.P("\t_result = binder.NewProxyBinder(p.Remote.Transport(), p.Remote.Identity(), _handle)")
		return
	}

	if info.NeedsCast {
		goType := resolveTypeRef(typeRef, retType)
		f.P("\t_raw, _err := %s", readExpr)
		f.P("\tif _err != nil {")
		f.P("\t\treturn _result, _err")
		f.P("\t}")
		f.P("\t_result = %s(_raw)", goType)
		return
	}

	f.P("\t_result, _err = %s", readExpr)
	f.P("\tif _err != nil {")
	f.P("\t\treturn _result, _err")
	f.P("\t}")
}

// readArrayFromReply writes code to read an array return value from a reply parcel.
func readArrayFromReply(
	f *GoFile,
	retType *parser.TypeSpecifier,
	opts GenOptions,
	typeRef *TypeRefResolver,
) {
	elemType := elementTypeSpec(retType)

	// AIDL uses compact readByteArray for byte[], not per-element
	// ReadPaddedByte. This matches the NDK AParcel_readByteArray format.
	if elemType.Name == "byte" {
		f.P("\t_result, _err = _reply.ReadByteArray()")
		f.P("\tif _err != nil {")
		f.P("\t\treturn _result, _err")
		f.P("\t}")
		return
	}

	goType := resolveTypeRef(typeRef, retType)
	elemInfo := marshalForTypeWithCycleCheck(elemType, opts, typeRef)

	f.P("\t_count, _err := _reply.ReadInt32()")
	f.P("\tif _err != nil {")
	f.P("\t\treturn _result, _err")
	f.P("\t}")
	f.P("\tif _count > 1000000 {")
	f.P("\t\treturn _result, fmt.Errorf(\"array count too large: %%d\", _count)")
	f.P("\t}")
	f.P("")
	f.P("\tif _count >= 0 {")
	f.P("\t\t_result = make(%s, _count)", goType)
	f.P("\t\tfor _i := int32(0); _i < _count; _i++ {")

	elemReadExpr := elemInfo.ReadExpr

	if elemReadExpr == "" {
		// Opaque/unresolvable element type: skip each element's parcelable
		// data on the wire so the parcel position stays consistent.
		if isLikelyParcelable(elemType) {
			f.AddImport("github.com/AndroidGoLab/binder/parcel", "")
			f.P("\t\t\tif _, _err = _reply.ReadInt32(); _err != nil {")
			f.P("\t\t\t\treturn _result, _err")
			f.P("\t\t\t}")
			f.P("\t\t\t_endPos, _err := parcel.ReadParcelableHeader(_reply)")
			f.P("\t\t\tif _err != nil {")
			f.P("\t\t\t\treturn _result, _err")
			f.P("\t\t\t}")
			f.P("\t\t\tparcel.SkipToParcelableEnd(_reply, _endPos)")
		}
		f.P("\t\t}")
		f.P("\t}")
		return
	}

	switch {
	case strings.Contains(elemReadExpr, ".UnmarshalParcel"):
		// Read non-null indicator, then the element. On API 36+ the
		// element is wrapped in a size-prefixed header (writeTypedList
		// format change). ReadTypedListElementHeader checks the API
		// level and reads the header when present.
		f.AddImport("github.com/AndroidGoLab/binder/parcel", "")
		f.P("\t\t\t_nonNull, _err := _reply.ReadInt32()")
		f.P("\t\t\tif _err != nil {")
		f.P("\t\t\t\tbreak // end of inline elements (may be fewer than count)")
		f.P("\t\t\t}")
		f.P("\t\t\tif _nonNull != 0 {")
		f.P("\t\t\t\tif _reply.Position() >= _reply.Len() {")
		f.P("\t\t\t\t\tbreak // truncated list (element non-null but no data remaining)")
		f.P("\t\t\t\t}")
		f.P("\t\t\t\t_elemEnd, _hasElemHeader := parcel.ReadTypedListElementHeader(_reply)")
		f.P("\t\t\t\tif _hasElemHeader {")
		f.P("\t\t\t\t\t// Enforce element boundary so UnmarshalParcel")
		f.P("\t\t\t\t\t// stops at the size-prefix envelope edge.")
		f.P("\t\t\t\t\t_savedLimit := _reply.ReadLimit()")
		f.P("\t\t\t\t\t_reply.SetReadLimit(_elemEnd)")
		f.P("\t\t\t\t\t_ = _result[_i].UnmarshalParcel(_reply)")
		f.P("\t\t\t\t\t_reply.SetReadLimit(_savedLimit)")
		f.P("\t\t\t\t\tparcel.SkipToParcelableEnd(_reply, _elemEnd)")
		f.P("\t\t\t\t} else {")
		f.P("\t\t\t\t\tif _err = _result[_i].UnmarshalParcel(_reply); _err != nil {")
		f.P("\t\t\t\t\t\treturn _result, _err")
		f.P("\t\t\t\t\t}")
		f.P("\t\t\t\t}")
		f.P("\t\t\t}")
	case elemInfo.IsInterface:
		proxyConstructor := interfaceProxyConstructor(typeRef, elemType)
		f.P("\t\t\t_handle, _err := %s", elemReadExpr)
		f.P("\t\t\tif _err != nil {")
		f.P("\t\t\t\treturn _result, _err")
		f.P("\t\t\t}")
		f.P("\t\t\t_result[_i] = %s(binder.NewProxyBinder(p.Remote.Transport(), p.Remote.Identity(), _handle))", proxyConstructor)
	case elemInfo.IsIBinder:
		f.P("\t\t\t_handle, _err := %s", elemReadExpr)
		f.P("\t\t\tif _err != nil {")
		f.P("\t\t\t\treturn _result, _err")
		f.P("\t\t\t}")
		f.P("\t\t\t_result[_i] = binder.NewProxyBinder(p.Remote.Transport(), p.Remote.Identity(), _handle)")
	case elemInfo.NeedsCast:
		elemGoType := resolveTypeRef(typeRef, elemType)
		f.P("\t\t\t_raw, _err := %s", elemReadExpr)
		f.P("\t\t\tif _err != nil {")
		f.P("\t\t\t\treturn _result, _err")
		f.P("\t\t\t}")
		f.P("\t\t\t_result[_i] = %s(_raw)", elemGoType)
	default:
		f.P("\t\t\t_result[_i], _err = %s", elemReadExpr)
		f.P("\t\t\tif _err != nil {")
		f.P("\t\t\t\treturn _result, _err")
		f.P("\t\t\t}")
	}

	f.P("\t\t}")
	f.P("\t}")
}

// readOutParam writes code to read an out/inout parameter from a reply parcel.
// hasReturn indicates whether the enclosing method has a return value, which
// affects the shape of early-return error statements.
// outArrayIdx is a pointer to a counter for unique variable names when
// reading multiple array out-params in the same method.
func readOutParam(
	f *GoFile,
	param *parser.ParamDecl,
	hasReturn bool,
	opts GenOptions,
	typeRef *TypeRefResolver,
	outArrayIdx *int,
) {
	varName := sanitizeGoIdent(param.ParamName)

	// Handle array/List out-params by reading element-by-element.
	if param.Type.IsArray || param.Type.Name == "List" {
		readOutParamArray(f, param.Type, varName, hasReturn, opts, typeRef, outArrayIdx)
		return
	}

	if param.Type.Name == "Map" {
		readMapFromReply(f, param.Type, opts, typeRef, "_reply", varName, "\t", errReturnStr(hasReturn))
		return
	}

	info := marshalForTypeWithCycleCheck(param.Type, opts, typeRef)
	if info.ReadExpr == "" {
		return
	}

	errReturn := errReturnStr(hasReturn)

	if strings.Contains(info.ReadExpr, ".UnmarshalParcel") {
		// Java's readTypedObject reads int32 null indicator before
		// calling createFromParcel. Skip unmarshaling for null (0).
		f.P("\t{")
		f.P("\t\t_nullInd, _err := _reply.ReadInt32()")
		f.P("\t\tif _err != nil {")
		f.P("\t\t\t%s", errReturn)
		f.P("\t\t}")
		f.P("\t\tif _nullInd != 0 {")
		unmarshalExpr := fmt.Sprintf(info.ReadExpr, varName)
		f.P("\t\t\tif _err = %s; _err != nil {", unmarshalExpr)
		f.P("\t\t\t\t%s", errReturn)
		f.P("\t\t\t}")
		f.P("\t\t}")
		f.P("\t}")
		return
	}

	if info.IsInterface {
		proxyConstructor := interfaceProxyConstructor(typeRef, param.Type)
		f.P("\t{")
		f.P("\t\t_handle, _err := %s", info.ReadExpr)
		f.P("\t\tif _err != nil {")
		f.P("\t\t\t%s", errReturn)
		f.P("\t\t}")
		f.P("\t\t%s = %s(binder.NewProxyBinder(p.Remote.Transport(), p.Remote.Identity(), _handle))", varName, proxyConstructor)
		f.P("\t}")
		return
	}

	if info.IsIBinder {
		f.P("\t{")
		f.P("\t\t_handle, _err := %s", info.ReadExpr)
		f.P("\t\tif _err != nil {")
		f.P("\t\t\t%s", errReturn)
		f.P("\t\t}")
		f.P("\t\t%s = binder.NewProxyBinder(p.Remote.Transport(), p.Remote.Identity(), _handle)", varName)
		f.P("\t}")
		return
	}

	if info.NeedsCast {
		goType := resolveTypeRef(typeRef, param.Type)
		f.P("\t{")
		f.P("\t\t_raw, _err := %s", info.ReadExpr)
		f.P("\t\tif _err != nil {")
		f.P("\t\t\t%s", errReturn)
		f.P("\t\t}")
		f.P("\t\t%s = %s(_raw)", varName, goType)
		f.P("\t}")
		return
	}

	// Primitive out-params: read from reply and assign back.
	f.P("\t%s, _err = %s", varName, info.ReadExpr)
	f.P("\tif _err != nil {")
	f.P("\t\t%s", errReturn)
	f.P("\t}")
}

// readOutParamArray writes code to read an array/List out-param from a reply parcel.
func readOutParamArray(
	f *GoFile,
	ts *parser.TypeSpecifier,
	varName string,
	hasReturn bool,
	opts GenOptions,
	typeRef *TypeRefResolver,
	outArrayIdx *int,
) {
	elemType := elementTypeSpec(ts)

	// AIDL uses compact readByteArray for byte[], not per-element
	// ReadPaddedByte. This matches the NDK AParcel_readByteArray format.
	if elemType.Name == "byte" {
		errReturn := "\t\treturn _err"
		if hasReturn {
			errReturn = "\t\treturn _result, _err"
		}
		f.P("\t%s, _err = _reply.ReadByteArray()", varName)
		f.P("\tif _err != nil {")
		f.P("%s", errReturn)
		f.P("\t}")
		return
	}

	goType := resolveTypeRef(typeRef, ts)
	elemInfo := marshalForTypeWithCycleCheck(elemType, opts, typeRef)

	countVar := fmt.Sprintf("_outCount%d", *outArrayIdx)
	*outArrayIdx++

	errReturn := "\t\treturn _err"
	if hasReturn {
		errReturn = "\t\treturn _result, _err"
	}

	f.P("\t%s, _err := _reply.ReadInt32()", countVar)
	f.P("\tif _err != nil {")
	f.P("%s", errReturn)
	f.P("\t}")
	f.P("\tif %s > 1000000 {", countVar)
	if hasReturn {
		f.P("\t\treturn _result, fmt.Errorf(\"array count too large: %%d\", %s)", countVar)
	} else {
		f.P("\t\treturn fmt.Errorf(\"array count too large: %%d\", %s)", countVar)
	}
	f.P("\t}")
	f.P("\tif %s >= 0 {", countVar)
	f.P("\t\t%s = make(%s, %s)", varName, goType, countVar)

	elemReadExpr := elemInfo.ReadExpr
	if elemReadExpr != "" {
		innerErrReturn := strings.Replace(errReturn, "\t\t", "\t\t\t\t", 1)
		f.P("\t\tfor _i := int32(0); _i < %s; _i++ {", countVar)
		switch {
		case strings.Contains(elemReadExpr, ".UnmarshalParcel"):
			// NDK AIDL wire format: int32 non-null indicator before each
			// parcelable element, matching readTypedObject semantics.
			f.P("\t\t\tif _, _err = _reply.ReadInt32(); _err != nil {")
			f.P("\t\t\t%s", innerErrReturn)
			f.P("\t\t\t}")
			f.P("\t\t\tif _err = %s[_i].UnmarshalParcel(_reply); _err != nil {", varName)
			f.P("\t\t\t%s", innerErrReturn)
			f.P("\t\t\t}")
		case elemInfo.IsInterface:
			proxyConstructor := interfaceProxyConstructor(typeRef, elemType)
			f.P("\t\t\t_handle, _err := %s", elemReadExpr)
			f.P("\t\t\tif _err != nil {")
			f.P("\t\t\t%s", innerErrReturn)
			f.P("\t\t\t}")
			f.P("\t\t\t%s[_i] = %s(binder.NewProxyBinder(p.Remote.Transport(), p.Remote.Identity(), _handle))", varName, proxyConstructor)
		case elemInfo.IsIBinder:
			f.P("\t\t\t_handle, _err := %s", elemReadExpr)
			f.P("\t\t\tif _err != nil {")
			f.P("\t\t\t%s", innerErrReturn)
			f.P("\t\t\t}")
			f.P("\t\t\t%s[_i] = binder.NewProxyBinder(p.Remote.Transport(), p.Remote.Identity(), _handle)", varName)
		case elemInfo.NeedsCast:
			elemGoType := resolveTypeRef(typeRef, elemType)
			f.P("\t\t\t_raw, _err := %s", elemReadExpr)
			f.P("\t\t\tif _err != nil {")
			f.P("\t\t\t%s", innerErrReturn)
			f.P("\t\t\t}")
			f.P("\t\t\t%s[_i] = %s(_raw)", varName, elemGoType)
		default:
			f.P("\t\t\t%s[_i], _err = %s", varName, elemReadExpr)
			f.P("\t\t\tif _err != nil {")
			f.P("\t\t\t%s", innerErrReturn)
			f.P("\t\t\t}")
		}
		f.P("\t\t}")
	}

	f.P("\t}")
}

// mapKeyTypeSpec returns the key type from a Map<K,V> type specifier.
// Falls back to a String type if no type arguments are present.
func mapKeyTypeSpec(ts *parser.TypeSpecifier) *parser.TypeSpecifier {
	if len(ts.TypeArgs) >= 1 {
		return ts.TypeArgs[0]
	}
	return &parser.TypeSpecifier{Name: "String"}
}

// mapValTypeSpec returns the value type from a Map<K,V> type specifier.
// Falls back to a String type if no type arguments are present.
func mapValTypeSpec(ts *parser.TypeSpecifier) *parser.TypeSpecifier {
	if len(ts.TypeArgs) >= 2 {
		return ts.TypeArgs[1]
	}
	return &parser.TypeSpecifier{Name: "String"}
}

// writeMapToParcel writes map serialization code. Android serializes maps as:
// int32 count (or -1 for nil), then count pairs of (key, value).
// transportExpr is the Go expression for the binder transport, used when
// writing interface/IBinder map values (e.g., "p.Remote.Transport()" for
// proxies, "s.Transport" for stubs).
func writeMapToParcel(
	f *GoFile,
	ts *parser.TypeSpecifier,
	varName string,
	hasReturn bool,
	opts GenOptions,
	typeRef *TypeRefResolver,
	parcelVar string,
	indent string,
	transportExpr string,
) {
	keyType := mapKeyTypeSpec(ts)
	valType := mapValTypeSpec(ts)
	keyInfo := marshalForTypeWithCycleCheck(keyType, opts, typeRef)
	valInfo := marshalForTypeWithCycleCheck(valType, opts, typeRef)

	f.P("%sif %s == nil {", indent, varName)
	f.P("%s\t%s.WriteInt32(-1)", indent, parcelVar)
	f.P("%s} else {", indent)
	f.P("%s\t%s.WriteInt32(int32(len(%s)))", indent, parcelVar, varName)

	if keyInfo.WriteExpr != "" && valInfo.WriteExpr != "" {
		f.P("%s\tfor _k, _v := range %s {", indent, varName)

		// When the map Go type has any for key or value,
		// the range variables are any and need type assertions
		// to match what the write expressions expect.
		keyVar := mapRangeVarWithAssertion("_k", keyType, ts, 0, typeRef)
		valVar := mapRangeVarWithAssertion("_v", valType, ts, 1, typeRef)
		writeKeyExpr := fmt.Sprintf(keyInfo.WriteExpr, keyVar)
		writeKeyExpr = strings.ReplaceAll(writeKeyExpr, "_data", parcelVar)
		writeValExpr := fmt.Sprintf(valInfo.WriteExpr, valVar)
		writeValExpr = strings.ReplaceAll(writeValExpr, "_data", parcelVar)

		errReturn := errReturnStr(hasReturn)
		writeMapElemExpr(f, keyInfo, writeKeyExpr, errReturn, indent+"\t\t", parcelVar, transportExpr, keyVar)
		writeMapElemExpr(f, valInfo, writeValExpr, errReturn, indent+"\t\t", parcelVar, transportExpr, valVar)

		f.P("%s\t}", indent)
	}

	f.P("%s}", indent)
}

// writeMapElemExpr writes a single element write expression (key or value)
// with appropriate error handling.
// transportExpr is the Go expression for the transport (e.g.,
// "p.Remote.Transport()" for proxies, "s.Transport" for stubs).
// elemVar is the Go variable expression for the element (e.g., "_k", "_v").
func writeMapElemExpr(
	f *GoFile,
	info MarshalInfo,
	writeExpr string,
	errReturn string,
	indent string,
	parcelVar string,
	transportExpr string,
	elemVar string,
) {
	switch {
	case strings.Contains(info.WriteExpr, ".MarshalParcel"):
		// NDK AIDL wire format: int32(1) non-null indicator before each
		// parcelable element, matching writeTypedObject semantics.
		f.P("%s%s.WriteInt32(1)", indent, parcelVar)
		f.P("%sif _err := %s; _err != nil {", indent, writeExpr)
		f.P("%s\t%s", indent, errReturn)
		f.P("%s}", indent)
	case info.IsInterface:
		// Interface elements must use WriteBinderToParcel to handle
		// both remote proxies and local stubs transparently.
		f.P("%sbinder.WriteBinderToParcel(ctx, %s, %s.AsBinder(), %s)", indent, parcelVar, elemVar, transportExpr)
	case info.IsIBinder:
		// IBinder elements must use WriteBinderToParcel.
		f.P("%sbinder.WriteBinderToParcel(ctx, %s, %s, %s)", indent, parcelVar, elemVar, transportExpr)
	default:
		f.P("%s%s", indent, writeExpr)
	}
}

// readMapFromReply writes map deserialization code. Reads int32 count,
// then count pairs of (key, value) into a Go map.
// errReturn is the error return statement to use (e.g., "return _result, _err"
// or "return _err" for void methods).
func readMapFromReply(
	f *GoFile,
	ts *parser.TypeSpecifier,
	opts GenOptions,
	typeRef *TypeRefResolver,
	parcelVar string,
	destVar string,
	indent string,
	errReturn string,
) {
	goType := resolveTypeRef(typeRef, ts)
	keyType := mapKeyTypeSpec(ts)
	valType := mapValTypeSpec(ts)
	keyInfo := marshalForTypeWithCycleCheck(keyType, opts, typeRef)
	valInfo := marshalForTypeWithCycleCheck(valType, opts, typeRef)

	keyReadExpr := strings.ReplaceAll(keyInfo.ReadExpr, "_reply", parcelVar)
	valReadExpr := strings.ReplaceAll(valInfo.ReadExpr, "_reply", parcelVar)

	// Use a block scope so _mapCount doesn't collide when multiple
	// Map out-params are read in the same method body.
	f.P("%s{", indent)
	f.P("%s\t_mapCount, _err := %s.ReadInt32()", indent, parcelVar)
	f.P("%s\tif _err != nil {", indent)
	f.P("%s\t\t%s", indent, errReturn)
	f.P("%s\t}", indent)

	f.P("%s\tif _mapCount >= 0 {", indent)
	f.P("%s\t\t%s = make(%s, _mapCount)", indent, destVar, goType)

	valIsList := valType.IsArray || valType.Name == "List"
	keyIsList := keyType.IsArray || keyType.Name == "List"

	if (keyReadExpr != "" || keyIsList) && (valReadExpr != "" || valIsList) {
		f.P("%s\t\tfor _mi := int32(0); _mi < _mapCount; _mi++ {", indent)

		if keyIsList {
			writeMapReadArrayElem(f, keyType, "_mk", indent+"\t\t\t", opts, typeRef, parcelVar, errReturn)
		} else {
			writeMapReadElem(f, keyInfo, keyReadExpr, keyType, "_mk", indent+"\t\t\t", typeRef, parcelVar, "k", errReturn)
		}

		if valIsList {
			writeMapReadArrayElem(f, valType, "_mv", indent+"\t\t\t", opts, typeRef, parcelVar, errReturn)
		} else {
			writeMapReadElem(f, valInfo, valReadExpr, valType, "_mv", indent+"\t\t\t", typeRef, parcelVar, "v", errReturn)
		}

		f.P("%s\t\t\t%s[_mk] = _mv", indent, destVar)
		f.P("%s\t\t}", indent)
	}

	f.P("%s\t}", indent)
	f.P("%s}", indent)
}

// writeMapReadArrayElem generates inline array/List reading code for a map
// element (key or value). This handles the case where a map value is
// List<T> or T[] — a slice type that has no UnmarshalParcel method and
// must be read element-by-element.
// writeMapWriteArrayElem generates inline array write code for a map
// key or value that is a List/Array type.
func writeMapWriteArrayElem(
	f *GoFile,
	ts *parser.TypeSpecifier,
	varName string,
	indent string,
	opts GenOptions,
	typeRef *TypeRefResolver,
	parcelVar string,
	errReturn string,
) {
	elemType := elementTypeSpec(ts)
	elemInfo := marshalForTypeWithCycleCheck(elemType, opts, typeRef)

	f.P("%s%s.WriteInt32(int32(len(%s)))", indent, parcelVar, varName)
	if elemInfo.WriteExpr != "" {
		f.P("%sfor _, _arrItem := range %s {", indent, varName)
		switch {
		case strings.Contains(elemInfo.WriteExpr, ".MarshalParcel"):
			f.P("%s\t%s.WriteInt32(1)", indent, parcelVar)
			writeExpr := strings.ReplaceAll(elemInfo.WriteExpr, "_data", parcelVar)
			f.P("%s\tif _err := %s; _err != nil {", indent, fmt.Sprintf(writeExpr, "_arrItem"))
			f.P("%s\t\t%s", indent, errReturn)
			f.P("%s\t}", indent)
		default:
			writeExpr := strings.ReplaceAll(elemInfo.WriteExpr, "_data", parcelVar)
			f.P("%s\t%s", indent, fmt.Sprintf(writeExpr, "_arrItem"))
		}
		f.P("%s}", indent)
	}
}

func writeMapReadArrayElem(
	f *GoFile,
	ts *parser.TypeSpecifier,
	varName string,
	indent string,
	opts GenOptions,
	typeRef *TypeRefResolver,
	parcelVar string,
	errReturn string,
) {
	goType := resolveTypeRef(typeRef, ts)
	elemType := elementTypeSpec(ts)
	elemInfo := marshalForTypeWithCycleCheck(elemType, opts, typeRef)
	elemReadExpr := strings.ReplaceAll(elemInfo.ReadExpr, "_reply", parcelVar)

	f.P("%svar %s %s", indent, varName, goType)
	f.P("%s{", indent)
	f.P("%s\t_arrCount, _err := %s.ReadInt32()", indent, parcelVar)
	f.P("%s\tif _err != nil {", indent)
	f.P("%s\t\t%s", indent, errReturn)
	f.P("%s\t}", indent)
	f.P("%s\tif _arrCount >= 0 {", indent)
	f.P("%s\t\t%s = make(%s, _arrCount)", indent, varName, goType)

	if elemReadExpr != "" {
		f.P("%s\t\tfor _ai := int32(0); _ai < _arrCount; _ai++ {", indent)

		switch {
		case strings.Contains(elemInfo.ReadExpr, ".UnmarshalParcel"):
			f.P("%s\t\t\tif _, _err = %s.ReadInt32(); _err != nil {", indent, parcelVar)
			f.P("%s\t\t\t\t%s", indent, errReturn)
			f.P("%s\t\t\t}", indent)
			f.P("%s\t\t\tif _err = %s[_ai].UnmarshalParcel(%s); _err != nil {", indent, varName, parcelVar)
			f.P("%s\t\t\t\t%s", indent, errReturn)
			f.P("%s\t\t\t}", indent)
		case elemInfo.NeedsCast:
			elemGoType := resolveTypeRef(typeRef, elemType)
			f.P("%s\t\t\t_raw, _err := %s", indent, elemReadExpr)
			f.P("%s\t\t\tif _err != nil {", indent)
			f.P("%s\t\t\t\t%s", indent, errReturn)
			f.P("%s\t\t\t}", indent)
			f.P("%s\t\t\t%s[_ai] = %s(_raw)", indent, varName, elemGoType)
		default:
			f.P("%s\t\t\t%s[_ai], _err = %s", indent, varName, elemReadExpr)
			f.P("%s\t\t\tif _err != nil {", indent)
			f.P("%s\t\t\t\t%s", indent, errReturn)
			f.P("%s\t\t\t}", indent)
		}

		f.P("%s\t\t}", indent)
	}

	f.P("%s\t}", indent)
	f.P("%s}", indent)
}

// writeMapReadElem writes the code to read a single map element (key or value)
// into a local variable.
// rawPrefix disambiguates the intermediate _raw variable when both key and
// value use NeedsCast (e.g., "k" produces _rawK, "v" produces _rawV).
// errReturn is the error return statement (e.g., "return _result, _err").
func writeMapReadElem(
	f *GoFile,
	info MarshalInfo,
	readExpr string,
	ts *parser.TypeSpecifier,
	varName string,
	indent string,
	typeRef *TypeRefResolver,
	parcelVar string,
	rawPrefix string,
	errReturn string,
) {
	switch {
	case strings.Contains(info.ReadExpr, ".UnmarshalParcel"):
		// Parcelable types: declare variable, read null indicator, then
		// unmarshal in-place. UnmarshalParcel returns only error, not (T, error).
		goType := resolveTypeRef(typeRef, ts)
		f.P("%svar %s %s", indent, varName, goType)
		f.P("%sif _, _err = %s.ReadInt32(); _err != nil {", indent, parcelVar)
		f.P("%s\t%s", indent, errReturn)
		f.P("%s}", indent)
		f.P("%sif _err = %s.UnmarshalParcel(%s); _err != nil {", indent, varName, parcelVar)
		f.P("%s\t%s", indent, errReturn)
		f.P("%s}", indent)
	case info.NeedsCast:
		goType := resolveTypeRef(typeRef, ts)
		rawVar := fmt.Sprintf("_raw%s", strings.ToUpper(rawPrefix[:1])+rawPrefix[1:])
		f.P("%s%s, _err := %s", indent, rawVar, readExpr)
		f.P("%sif _err != nil {", indent)
		f.P("%s\t%s", indent, errReturn)
		f.P("%s}", indent)
		f.P("%s%s := %s(%s)", indent, varName, goType, rawVar)
	default:
		f.P("%s%s, _err := %s", indent, varName, readExpr)
		f.P("%sif _err != nil {", indent)
		f.P("%s\t%s", indent, errReturn)
		f.P("%s}", indent)
	}
}

// mapRangeVarWithAssertion returns the variable expression to use for a map
// range variable. If the map's Go type has any for this position
// (key at index 0, value at index 1), but the element TypeSpec resolves to a
// concrete Go type, a type assertion is appended (e.g., "_k.(string)").
func mapRangeVarWithAssertion(
	varName string,
	elemType *parser.TypeSpecifier,
	mapTS *parser.TypeSpecifier,
	position int,
	typeRef *TypeRefResolver,
) string {
	// Determine the Go type of the element from the map's type args.
	mapGoKeyType := "any"
	mapGoValType := "any"
	switch {
	case typeRef != nil:
		if len(mapTS.TypeArgs) >= 1 {
			mapGoKeyType = typeRef.GoTypeRef(mapTS.TypeArgs[0])
		}
		if len(mapTS.TypeArgs) >= 2 {
			mapGoValType = typeRef.GoTypeRef(mapTS.TypeArgs[1])
		}
	case len(mapTS.TypeArgs) >= 2:
		mapGoKeyType = AIDLTypeToGo(mapTS.TypeArgs[0])
		mapGoValType = AIDLTypeToGo(mapTS.TypeArgs[1])
	}

	var mapElemGoType string
	if position == 0 {
		mapElemGoType = mapGoKeyType
	} else {
		mapElemGoType = mapGoValType
	}

	// If the map element is already a concrete type, no assertion needed.
	if mapElemGoType != "any" {
		return varName
	}

	// Determine the concrete type the write expression expects.
	concreteGoType := resolveTypeRef(typeRef, elemType)
	if concreteGoType == "" || concreteGoType == "any" {
		return varName
	}

	return varName + ".(" + concreteGoType + ")"
}

// errReturnStr returns the appropriate error return statement based on
// whether the enclosing method has a return value.
func errReturnStr(hasReturn bool) string {
	if hasReturn {
		return "return _result, _err"
	}
	return "return _err"
}

// methodSignature returns the Go signature for a method declaration.
// Identity parameters (callingPackage, attributionTag, etc.) are excluded
// to match the proxy method signatures, which auto-fill them from
// CallerIdentity.
func methodSignature(
	m *parser.MethodDecl,
	typeRef *TypeRefResolver,
) string {
	goName := AIDLToGoName(m.MethodName)
	hasReturn := m.ReturnType != nil && m.ReturnType.Name != "void"

	regularParams, _ := classifyParams(m.Params)

	var params []string
	params = append(params, "ctx context.Context")
	for _, p := range regularParams {
		goType := resolveTypeRef(typeRef, p.Type)
		params = append(params, sanitizeGoIdent(p.ParamName)+" "+goType)
	}

	paramStr := strings.Join(params, ", ")

	if hasReturn {
		goRetType := resolveTypeRef(typeRef, m.ReturnType)
		return fmt.Sprintf("%s(%s) (%s, error)", goName, paramStr, goRetType)
	}
	return fmt.Sprintf("%s(%s) error", goName, paramStr)
}

// interfaceProxyConstructor returns the Go expression for constructing a proxy
// for an interface type. For example, IFoo -> "NewFooProxy", and for
// cross-package references, "pkg.NewFooProxy".
func interfaceProxyConstructor(
	typeRef *TypeRefResolver,
	ts *parser.TypeSpecifier,
) string {
	goType := resolveTypeRef(typeRef, ts)
	// Strip leading pointer — @nullable interfaces resolve to *IFoo
	// but the proxy constructor is NewFooProxy (no pointer).
	goType = strings.TrimPrefix(goType, "*")

	// If the type is package-qualified (e.g., "pkg.IFoo"), split off
	// the package prefix.
	pkg := ""
	typeName := goType
	if dotIdx := strings.LastIndex(goType, "."); dotIdx >= 0 {
		pkg = goType[:dotIdx]
		typeName = goType[dotIdx+1:]
	}

	// Remove leading "I" to derive the proxy name: IFoo -> FooProxy.
	proxyBase := typeName
	if strings.HasPrefix(typeName, "I") && len(typeName) > 1 {
		proxyBase = typeName[1:]
	}
	constructor := "New" + proxyBase + "Proxy"

	if pkg != "" {
		return pkg + "." + constructor
	}
	return constructor
}

// sanitizeGoIdent ensures an identifier is not a Go keyword.
var goKeywords = map[string]bool{
	"break": true, "default": true, "func": true, "interface": true,
	"select": true, "case": true, "defer": true, "go": true,
	"map": true, "struct": true, "chan": true, "else": true,
	"goto": true, "package": true, "switch": true, "const": true,
	"fallthrough": true, "if": true, "range": true, "type": true,
	"continue": true, "for": true, "import": true, "return": true,
	"var": true,
}

// goReservedIdents are identifiers that must be renamed to avoid conflicts
// with Go keywords, commonly imported package names, or generated variable
// names used in proxy method bodies.
var goReservedIdents = map[string]bool{
	"binder": true, "parcel": true, "context": true,
	"error": true, "string": true,
	// Proxy methods use "p" and stub methods use "s" as receiver names.
	"p": true, "s": true,
	// Built-in functions that must not be shadowed by parameter names.
	"len": true, "cap": true, "make": true, "new": true, "append": true,
	"copy": true, "close": true, "delete": true, "panic": true, "recover": true,
	"print": true, "println": true, "complex": true, "real": true, "imag": true,
	"clear": true, "min": true, "max": true,
	// Codegen-internal variable names used in generated proxy/stub method bodies.
	"ctx": true, "_data": true, "_reply": true, "_err": true,
	"_result": true, "_code": true, "_identity": true,
}

func sanitizeGoIdent(name string) string {
	if goKeywords[name] || goReservedIdents[name] {
		return name + "_"
	}
	return name
}

// isLikelyParcelable returns true if the type specifier looks like a
// parcelable type on the wire, even though its Go type resolved to
// any (e.g., because it's a Java-originated forward-declared
// parcelable not in the type registry). Primitives, IBinder, List, Map,
// and void are not parcelable.
func isLikelyParcelable(ts *parser.TypeSpecifier) bool {
	if ts == nil {
		return false
	}
	switch ts.Name {
	case "", "void",
		"int", "long", "boolean", "byte", "float", "double", "char",
		"String", "IBinder", "ParcelFileDescriptor",
		"List", "Map":
		return false
	}
	return true
}
