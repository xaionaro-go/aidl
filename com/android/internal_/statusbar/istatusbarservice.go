// NewStatusBarServiceStub creates a server-side IStatusBarService wrapping the given
// server implementation. The returned value satisfies IStatusBarService
// and can be passed to proxy methods; its AsBinder() returns a
// *binder.StubBinder that is auto-registered with the binder
// driver on first use.
func NewStatusBarServiceStub(
	impl IStatusBarServiceServer,
) IStatusBarService {
	wrapper := &statusBarServiceStubWrapper{impl: impl}
	stub := &StatusBarServiceStub{Impl: wrapper}
	wrapper.stubBinder = binder.NewStubBinder(stub)
	return wrapper
}
